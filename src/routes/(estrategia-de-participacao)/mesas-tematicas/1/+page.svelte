	<h4 class="text-xl font-bold pt-6">Sumário da discussão dos participantes</h4>
	<p>
		A Mesa Temática subordinada ao tema da “Internacionalização e integração da economia Portuguesa
		no mundo” abordou a evolução da globalização a nível global e na economia portuguesa, a
		importância do transporte aéreo no transporte de mercadorias, as exportações e o investimento
		direto estrangeiro e a circulação e atração de talento associadas às novas formas de trabalho e
		turismo.
	</p>
	<p>
		O crescimento da globalização abrandou a nível mundial, desde a crise financeira internacional
		de 2008, com o comércio internacional a crescer menos do que o PIB. Portugal tem estado em
		contraciclo, com um aumento das exportações mais pronunciado que o crescimento do PIB, tendo o
		peso das exportações no PIB ultrapassado os 50% em 2022. Os períodos de maior crescimento
		económico em Portugal estiveram associados a um maior envolvimento na globalização.
	</p>
	<p>
		Portugal encontra-se num estado menos avançado que a média dos países da União Europeia (UE),
		tendo como principal exportação os produtos de manufatura avançados e serviços, onde se destaca
		o turismo, contrastando com uma maior concentração da EU em atividades inovadoras. A melhoria no
		perfil das exportações requer uma melhoria da competitividade das empresas portuguesas, da
		qualidade dos produtos e das qualificações da força laboral. O reduzido número de grandes
		empresas é uma barreira à exportação.
	</p>
	<p>
		Foi discutida a questão da dependência produtiva da Europa face a outras localizações e como
		isso poderia influenciar a estratégia da economia portuguesa. É fundamental perceber as
		tendências e mudanças nas cadeias globais de valor e identificar as melhores oportunidades para
		nelas participar nos segmentos de maior valor.
	</p>
	<p>
		O turismo, para além da sua importância no PIB, no emprego e na balança comercial tem dado um
		contributo fundamental para a promoção de Portugal no exterior e, assim, para o arrastamento do
		crescimento das exportações nos países de origem dos turistas. A conectividade internacional é
		essencial para manter essa dinâmica positiva entre turismo e exportações, permitindo também uma
		maior diversificação da estrutura produtiva da economia portuguesa.
	</p>
	<p>
		A análise das tendências e a forma como elas impactam no nosso país são muito relevantes e devem
		ser tidas em conta na alocação dos recursos de modo a alavancar e aproveitar essas tendências.
		No entanto, para definir a estratégia para o país e o modelo de aeroporto que queremos é mais
		importante analisar escolhas do que tendências.
	</p>
	<p>
		A escolha na alocação dos recursos é da maior importância para atrair e fixar talento, sobretudo
		porque o nosso futuro vai depender das pessoas mais do que dos bens. Fará sentido apostar num
		modelo de terciarização com valor. Atrair e reter agentes inovadores da economia do
		conhecimento. A localização do aeroporto será decisiva para este modelo da economia que
		queremos. A escolha da opção para a construção do aeroporto, pelo seu carater estrutural e de
		longo prazo, deve ter em conta as tendências e a estratégia para o país. Em particular,
		considerou-se importante identificar as áreas onde podemos crescer sem sermos imitados,
		alinhados não só com a procura presente, mas com a procura futura, e fomentar um ambiente de
		negócios que responda a essa procura.
	</p>
	<p>
		A ideia de pequena economia foi contestada, devendo valorizar-se o facto de estarmos num dos
		maiores blocos económicos do mundo, e o facto de, dentro da UE, quer pela população quer pelo
		território, pertencermos ao grupo de países de média dimensão.
	</p>
	<p>
		O transporte aéreo é rápido, cobre longas distâncias, e é utilizado por produtos de alto valor
		unitário. Embora o transporte de carga não seja a função principal do aeroporto, é preciso ter
		em conta essa função, que tem um enorme potencial para crescer. Os dados das exportações de
		mercadorias portuguesas mostram que os meios de transportes mais utilizados (por ordem
		decrescente) são o rodoviário, o marítimo, o aéreo e ferroviário. No entanto, por custo
		decrescente de valor unitário (€/kg) destaca-se o transporte aéreo (muito superior), o
		ferroviário, o rodoviário e, por último, marítimo. A maquinaria e equipamento de tratamento
		informático e da informação, os produtos farmacêuticos, e o vestuário são os produtos/sectores
		mais expedidos por via aérea. A customização na produção tem vindo a crescer em detrimento da
		produção em massa, e isso poderá ser importante para este tipo de exportação. Foi ainda referido
		que a exportação de máquinas e equipamentos é geralmente acompanhada por técnicos especializados
		na instalação e que Portugal é “campeão de pequenas séries” no vestuário. Destacou-se também a
		produção para e-commerce, uma dimensão da distribuição que tem crescido exponencialmente, o que
		valoriza muito as infraestruturas aeroportuárias. É fundamental que haja espaço na
		infraestrutura para albergar o aumento da carga aérea (equipamento de grande valor acrescentado,
		nomeadamente como resultado de práticas de <i>just-in-time</i> e de <i>e-commerce</i>).
	</p>
	<p>
		Há uma grande correlação entre turismo e exportações, porque grande parte do turismo é também
		executivo. Por exemplo, o crescimento de exportação de vinho para o Brasil e para os Estados
		Unidos acompanha o crescimento de turistas destes mercados.
	</p>
	<p>
		O valor de investimento direto estrangeiro (IDE) tem tido um aumento significativo. Os contratos
		feitos têm sido mais diversificados em termos de nacionalidades, tanto a nível europeu como de
		outros continentes, neste caso com destaque para os Estados Unidos, o Brasil, a Coreia do Sul e
		o Japão. Em termos de IDE, tem-se assistido também a uma maior diversificação. Também as
		empresas exportadoras, a partir de uma certa dimensão, têm conseguido diversificar os mercados
		rapidamente, refletindo a sua capacidade inovadora. Os <i>business service centres</i> tiveram
		um grande crescimento, devendo destacar nessa área os centros de desenvolvimento de
		<i>software</i> e o sector financeiro.
	</p>
	<p>
		Na atração de IDE e para o desenvolvimento das empresas exportadoras é fundamental ter um
		aeroporto ligado a um número máximo de geografias, a escala do aeroporto deve permitir ter
		acesso a voos do mundo inteiro. A conectividade internacional e a frequência de ligações
		ponto-a-ponto são essenciais para que os investidores considerem nas suas escolhas
		Lisboa/Portugal. Os executivos em viagens de negócios precisam de ligações rápidas ao centro da
		cidade. O processamento alfandegário tem que ser rápido e a qualidade de todos os serviços
		aeroportuários é também essencial.
	</p>
	<p>
		No sector do turismo destaca-se o aumento de visitantes americanos e brasileiros, sendo que
		destes 70% dos que passam no aeroporto não ficam em Portugal. A estratégia do aeroporto terá que
		estar alinhada com a estratégia das companhias aéreas e os mercados de longa distância são
		prioritários para o turismo, cujo grande objetivo é diversificar os mercados. Os mercados de
		longa distância são também os que ficam mais tempo no país e que acabam por contribuir mais para
		a economia. O limite de capacidade do Aeroporto Humberto Delgado está a fazer com que sejam
		recusados voos da Coreia, do Japão e de outros mercados do interesse para o turismo português.
	</p>
	<p>
		A estratégia de desenvolvimento deverá basear-se nas nossas forças, nomeadamente no <i
			>lifestyle</i
		>
		ou
		<i>well-being</i>, que é o maior ativo de Portugal, dado que é uma característica do país que
		não pode ser imitada facilmente. Nesta abordagem, reside o sucesso de projetos como o campus da
		Nova SBE em Carcavelos. A vantagem competitiva do <i>lifestyle</i> também se reflete no facto de
		Lisboa surgir com a terceira melhor cidade do mundo para <i>remote working</i>.
	</p>
	<p>
		A sustentabilidade ambiental e social ganhará importância no futuro. O trabalho à distância
		poderá reduzir as deslocações, mas ainda assim vão existir locais de encontro/aglomeração
		tornando essencial a existência de boas ligações aéreas (por exemplo, para reuniões com
		clientes). O digital evita algumas deslocações, mas proporciona outras viagens porque as pessoas
		podem estar a trabalhar em qualquer lado. A tendência <i>bleisure</i> ou <i>blended travel</i>
		(uma mistura entre negócios – <i>business</i> – e lazer – <i>leisure</i> e outras atividades)
		aumenta a importância da facilidade de viajar. O futuro económico do país passará pelo
		<i>blended</i>
		dos usos funcionais urbanos. Uma habitação já não é só uma habitação, e uma empresa também poderá
		ter mais vocações. Um dos <i>basics</i> de segurança será a capacidade de ligação internacional.
	</p>
	<p>
		Em suma, na reunião foram discutidos vários tópicos relacionados com a economia portuguesa,
		incluindo o papel do transporte aéreo nas exportações, a importância do aeroporto de Lisboa, a
		estratégia de exportação e investimento produtivo, e o trabalho remoto. Foram também discutidos
		vários fatores que afetam a competitividade de Portugal, incluindo a qualidade do transporte e
		da mobilidade, a diversificação dos mercados de exportação, e a necessidade de gerar valor
		acrescentado. Também foi mencionada a importância de um aeroporto que esteja ligado a um número
		máximo de geografias e que tenha capacidade para albergar o aumento da carga aérea. Houve
		consenso sobre a necessidade de alinhar a estratégia do aeroporto com a estratégia do país e da
		cidade.
	</p>
