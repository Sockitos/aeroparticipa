	<h4>Ideias chave:</h4>
	<ul>
		<li>O hub permite ligações diretas (conectividade) a um maior número de destinos.</li>
		<li>
			O atual contexto de privatização da TAP terá impacte sobre o desenvolvimento do hub em Lisboa.
		</li>
	</ul>
	<h4>Sumário da discussão dos participantes</h4>
	<p><b>O hub é bom ou mau para o caso português?</b></p>
	<p>O hub é a melhor opção e positiva para o caso português.</p>
	<p>
		O hub permite ligações diretas (conetividade) a maior número de destinos. O hub da TAP em Lisboa
		tem sido um dos motores do desenvolvimento turístico de Lisboa. A TAP oferece a possibilidade
		stopover, em Lisboa, de alguns dias, o que tem permitido a muitos passageiros ficarem a conhecer
		Lisboa, e voltarem. Assim, o hub é bom para o caso português. Aliás, o atual aeroporto, mesmo
		sem condições, é já um hub reconhecido.
	</p>
	<p>
		O hub é desenvolvido por uma companha aérea, e não por pelo gestor de infraestrutura. O
		desenvolvimento do hub depende de uma companhia que estabeleça a base no novo aeroporto de
		Lisboa. Sem companhia de transporte aéreo não há hub. Portanto, o atual contexto de privatização
		da TAP lança dúvidas sobre a existência de um hub em Lisboa. Em função do investidor poderá não
		fazer sentido a existência de hub (ex.: se o comprador for a IAG, os participantes referem que
		não haverá hub em Lisboa).
	</p>
	<p>
		Na ausência de uma companhia aérea que faça de Lisboa a base, o aeroporto poderá não ser hub. A
		sua capacidade ficará ocupada por companhias que o utilizam para voos ponto a ponto. Neste caso,
		o tipo de voos e passageiros serão naturalmente diferentes. Os voos serão principalmente de
		média distância, ou low cost ou <i>feeder</i> para outro hub. E haverá redução de conetividade. Outros
		fatores que influenciam a existência de hub, é o crescimento do tráfego aéreo, que nos próximos anos
		poderá ser limitado por vários fatores, includingo restrições ambientais, custos do combustível ou
		outros.
	</p>
	<p>
		Uma vantagem de Lisboa no território europeu é que pode servir a Europa, África e América. Isto
		pode ser especialmente importante quando se consideram os impactos ambientais da aviação e o
		facto de os voos que partem de Lisboa exigirem uma hora a menos em comparação com outros
		aeroportos europeus. Os participantes salientaram que os pontos de corte para os níveis de
		emissões em viagens de curta, média e longa distância devem ser explorados juntamente com a
		possibilidade de utilizar aeronaves de corpo estreito para voos directos. Relativamente ao
		interior, deve ser considerada uma área de captação de 2-3h (de carro), mas, em comparação com
		outros aeroportos da UE, para Portugal, o interior terá sempre limitações. Finalmente, se um hub
		pudesse assegurar uma capacidade aérea suficiente, as companhias aéreas poderiam explorar novas
		oportunidades de negócio com a utilização de aeronaves para servir múltiplos pontos antes de
		regressarem ao seu aeroporto "de origem".
	</p>
