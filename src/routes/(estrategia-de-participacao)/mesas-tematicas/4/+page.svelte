		</li>
		<li>
			Novo aeroporto deve ser perspectivado como um atrator de serviços de valor acrescentado, no
			âmbito de um <b>ecossistema social e económico</b> de valor acrescentado.
		</li>
		<li>
			O novo aeroporto deve estar <b>localizado numa região bem estruturada</b>, de forma a
			minimizar os impactos ambientais e territoriais, associados com a construção do aeroporto.
		</li>
		<li>
			Destacada a importância de <b>integrar o transporte público de passageiros</b> na rede de transportes
			terrestres.
		</li>
	</ul>
	<h4>Sumário da discussão dos participantes</h4>
	<p>
		A Sessão de Debate destacou uma série de fatores relevantes. Em primeiro lugar, foi discutida a
		necessidade de se estabelecer uma visão estratégica para o setor de transporte aéreo em
		Portugal, a fim de orientar a tomada de decisões futuras. Além disso, foi destacado por alguns
		participantes que o novo aeroporto deve ser visto como um hub/local de facilitação e atração de
		serviços de valor acrescentado, pois a infraestrutura, per se, é um custo.
	</p>
	<p>
		O modelo do aeroporto também foi considerado um fator determinante, e deve estar relacionado com
		os serviços que se esperam que o aeroporto venha a ter. A criação de um ecossistema social e
		económico que crie um elevado valor acrescentado também foi destacada como uma prioridade.
	</p>
	<p>
		Outro ponto importante levantado no debate foi a necessidade de considerar o novo aeroporto num
		contexto de plataforma de lógica nacional e supranacional, com parcerias estáveis com destinos
		longínquos. Foi enfatizada a importância de haver mecanismos de atração de serviços e empresas,
		como um enquadramento fiscal favorável.
	</p>
	<p>
		O novo aeroporto apesar de vir a servir como hub para uma companhia aérea ou aliança, não deverá
		descurar as demais linhas aéreas. Em particular, as companhias low cost, que funcionam numa
		lógica ponto-a-ponto, são determinantes para estimular os fluxos de passageiros em turismo que é
		um dos principais setores económicos nacionais. De facto, o turismo foi considerado o motor da
		economia nacional, com sinais de aceleração e, portanto, é necessário garantir um aeroporto de
		elevado funcionamento.
	</p>
	<p>
		Também foi discutido que o novo aeroporto deve estar localizado numa região bem estruturada, de
		forma a minimizar os impactos ambientais e territoriais, associados com a construção do
		aeroporto.
	</p>
	<p>
		Foi ressaltado que o aeroporto Humberto Delgado está saturado e com qualidade completamente
		deteriorada, o que dificulta que venha a ser uma solução a longo prazo. Ainda assim, como a
		construção de um novo aeroporto vai demorar vários anos, o período de transição para o novo
		aeroporto deverá ser devidamente acautelado.
	</p>
	<p>
		O debate também considerou as mudanças tecnológicas, operacionais e procedimentais que devem vir
		a ocorrer no futuro, bem como as mudanças no perfil dos passageiros e na procura de mercados e
		segmentos. Foi destacada a importância da flexibilidade, e do desenvolvimento flexível, de forma
		a garantir a adaptabilidade às incertezas que o futuro nos reserva.
	</p>
	<p>
		A dimensão ambiental também foi discutida, em particular a esperada crescente limitação de voos,
		particularmente os europeus, devido a mecanismos como o do mercado de carbono. O desenvolvimento
		tecnológico será determinante para a evolução da aviação, em particular na Europa. Assim, se
		houver o desenvolvimento de aviões sem emissões significativas espera-se um crescimento do
		transporte aéreo. Pelo contrário, se os aviões continuarem a emitir poluentes, as restrições nos
		voos intraeuropeus serão progressivamente mais restritivas.
	</p>
	<p>
		O abastecimento de combustível não foi considerado um fator limitativo na localização do
		aeroporto. Porém, novas oportunidades energéticas, como o hidrogênio, levantam-se no horizonte
		da década de 30 ou 40, o que poderá alterar profundamente o paradigma de voo, nomeadamente no
		curto prazo.
	</p>
	<p>
		Por fim, foi destacada a importância de integrar o transporte público de passageiros na rede de
		transportes terrestres, assegurando a integração com a ferrovia e compatibilizando com o plano
		nacional ferroviário.
	</p>
	<h3>
		Resultados 2ª Sessão<br />Modelo Dual versus Modelo Single, quais as opções de transição?
	</h3>
	<h4>
		Coordenação: Rosário Macário<br />Relatores: Eng. Vasco Reis e Eng. Vitor Rocha
	</h4>
	<p>
		As várias opções foram discutidas em três mesas rotativas, permitindo a todos os participantes
		discutir e identificar as vantagens e inconvenientes das opções consideradas, que foram: um
		aeroporto Greenfield; uma opção Dual; Solução de transição.
	</p>
	<h4>Ideias chave:</h4>
	<ol>
		<li>
			A construção do novo aeroporto deve seguir uma lógica flexível (isto é, ter as componentes
			mínimas necessárias para entrar em funcionamento);
		</li>
		<li>
			Foi consensual que, no longo prazo, a região de Lisboa deverá ser servida por um único
			aeroporto hub. A existência de um único aeroporto é necessário para permitir a concentração de
			serviços e atividades numa única região e, desta forma, maximizar os benefícios do hubbing;
		</li>
		<li>
			A opção dual temporária é necessária para evitar a interrupção do fluxo aéreo em Lisboa
			durante a construção do novo aeroporto;
		</li>
		<li>
			A melhoria na ligação com outros modos de transporte também pode alargar a catchment area do
			green field. Adicionalmente, há claras vantagens ambientais, de saúde pública e de risco de
			acidente;
		</li>
		<li>
			A única opção para a manutenção da Portela seria como vertiport ou com um horário de
			funcionamento extremamente reduzido e limitado (à semelhança do aeroporto de London City).
		</li>
	</ol>
	<h4>Sumário da discussão dos participantes</h4>
	<p><b>Mesa 1: Opção Green Field</b></p>
	<p>
		Esta mesa debateu se o novo aeroporto de Lisboa deve ser construído como um green field airport.
		Entre as vantagens de um green field airport estão a visão de longo prazo, que não se limita à
		salvaguarda do turismo, e a oportunidade de promover o desenvolvimento econômico, captando
		saberes e novas tecnologias. Além disso, esse tipo de aeroporto pode ser uma alavanca para
		alterar o perfil económico da região, permitindo a junção das LCC e das companhias de bandeira
		para consolidar o HUB. Isso é importante, pois só há possibilidade de sobrevivência do HUB no
		longo prazo com uma solução que junte o médio curso e o longo curso, permitindo alavancar novas
		rotas no longo curso. A melhoria na ligação com outros modos de transporte também pode alargar a
		catchment area do green field. Adicionalmente, há claras vantagens ambientais, de saúde pública
		e de risco de acidente.
	</p>
	<p>
		Por outro lado, as desvantagens incluem o investimento e tempo de execução de obra em comparação
		com uma proposta dual, o que só faz sentido se analisado em termos de curto prazo. A mudança do
		HUB em primeiro lugar obriga a fechar a Portela para a mudança da LCC, o que pode ser um desafio
		para uma transição bem ponderada. Além disso, o tempo de transição para uma solução green field
		pode ser mais longo devido às dificuldades em investimento e tempo de construção. Existe um
		risco de investimento demasiado grande, e torna-se necessário encontrar soluções para
		suportá-lo. Pode existir uma afetação do passageiro sensível ao tempo e ao custo, o que pode
		condicionar a decisão do governo. Maior dificuldade no faseamento da transição para um novo
		aeroporto também é uma desvantagem importante.
	</p>
	<p>
		Dessa forma, é importante que o governo e/ou o potencial investidor façam algum esforço para
		minimizar o crescimento de taxas e atender às necessidades do turismo durante a transição.
		Embora seja necessário ponderar todos os prós e contras, é crucial encontrar soluções para
		suportar o investimento e garantir o sucesso a longo prazo do novo aeroporto de Lisboa.
	</p>
	<p><b>Mesa 2: Opção Dual</b></p>
	<p>Esta mesa debateu a opção dual. Várias opções duais foram analisadas, nomeadamente:</p>
	<ol>
		<li>
			<p>
				<b>Hub:</b> Novo Aeroporto<br />
				<b>Secundário:</b> Portela
			</p>
		</li>
		<li>
			<p>
				<b>Hub:</b> Portela<br />
				<b>Secundário:</b> Novo Aeroporto
			</p>
		</li>
		<li>
			<p>
				<b>Hub:</b> Novo Aeroporto<br />
				<b>Secundário:</b> Portela (com phase out após construção do Novo Aeroporto)
			</p>
		</li>
		<li>
			<p>
				<b>Hub:</b> Novo Aeroporto<br />
				<b>Secundário:</b> Portela mais 1 temporário enquanto Novo Aeroporto é construído
			</p>
		</li>
	</ol>
	<p>
		A opção #3 foi consensualmente escolhida – ou seja, a existência de uma solução dual temporária
		até à entrada em funcionamento do Novo Aeroporto. Neste cenário o Novo Aeroporto deverá ser
		posto em funcionamento o mais rapidamente possível e com os componentes mínimos necessários. Ou
		seja, a construção do novo aeroporto deve seguir uma lógica flexível (isto é, ter as componentes
		mínimas necessárias para entrar em funcionamento).
	</p>
	<p>
		A opção dual temporária é necessária para evitar a interrupção do fluxo aéreo em Lisboa durante
		a construção do novo aeroporto. A adaptação ou construção de um aeroporto secundário para
		permitir a transição, até à construção do novo aeroporto (Opção #4) não foi considerada viável
		por vários motivos, nomeadamente: custo afundado elevado, com hipóteses mínimas de recuperação
		no tempo de transição; ganhos de eficiência e eficácia operacional questionáveis; e o risco de
		as soluções temporárias se cristalizarem e, desta forma, protelarem a construção do Novo
		Aeroporto.
	</p>
	<p>
		De facto, foi algo consensual que, no longo prazo, a região de Lisboa deverá ser servida por um
		único aeroporto hub. A existência de um único aeroporto é necessário para permitir a
		concentração de serviços e atividades numa única região e, desta forma, maximizar os benefícios
		do hubbing. A opção dual limitaria o efeito de concentração e reduziria a natureza e a
		quantidade de serviços adicionais em cada um dos aeroportos. A solução dual pode impor várias
		limitações à criação de novos serviços, já que a Portela não tem capacidade para expandir. A
		solução dual implica uma maior carga ambiental do que a solução de um único aeroporto.
	</p>
	<p>
		Foi ainda discutido de que não há exemplos comparáveis de regiões como Lisboa (em termos de
		população) que possuam dois aeroportos internacionais. Aeroportos menores ou regionais são
		suficientes para atender às necessidades da região.
	</p>
	<p>
		Ainda assim foram reconhecidas algumas vantagens numa opção dual. A existência de dois
		aeroportos pode fornecer mais opções e flexibilidade para lidar com a variedade de segmentos
		aéreos. A manutenção da Portela permite a continuidade dos serviços prestados pelas companhias
		aéreas de baixo custo, que preferem a localização do aeroporto da Portela. A existência de dois
		aeroportos pode fornecer opções adicionais para serviços de logística e outras empresas de
		serviços de valor agregado. Na eventualidade de haver uma opção dual, a maioria dos
		participantes concordaram que a Portela deveria ser desativada. O aeroporto secundário poderá
		facilmente ser oferecido por um dos aeroportos existentes na região de Lisboa.
	</p>
	<p>
		A única opção para a manutenção da Portela seria como vertiport ou com um horário de
		funcionamento extremamente reduzido e limitado (à semelhança do aeroporto de London City).
	</p>
	<p><b>Mesa 3: Transição</b></p>
	<p>
		Nesta mesa foi debatida o modelo de transição do atual aeroporto da Portela para a solução
		futura, independentemente de ser greenfield ou dual.
	</p>
	<p>
		No caso da opção greenfield foram debatidas duas abordagens discutidas pelos participantes.
		Alguns participantes defenderam uma transição imediata para o novo aeroporto, argumentando que,
		apesar do receio, existem exemplos passados de sucesso em outros casos de aeroportos (como em
		Munique e Denver) e que é necessário avançar. A questão dos custos da transição seria dividida
		entre o governo ou os passageiros. Nessa abordagem, as decisões sobre uma única ou dupla
		configuração seriam mais importantes. Outros participantes defenderam uma transição em fases,
		com a maioria concordando que o sistema duplo deveria ser adotado até que o novo aeroporto
		estivesse pronto, com um horizonte de tempo de seis anos. Foi enfatizado que uma transição em
		fases deveria ter um cronograma claro e firme, que os envolvidos pudessem confiar e usar como
		base para suas operações. A colaboração e a confiança entre as partes interessadas foram
		apontadas como fundamentais nesse caso.
	</p>
	<p>
		Para a opção dual, alguns participantes apoiam a transferência das companhias aéreas de baixo
		custo (LCCs) para o novo aeroporto enquanto mantêm o hub na infraestrutura existente, enquanto
		outras apoiam a transferência do hub para o segundo aeroporto. No caso de manter o hub em
		Portela, o segundo aeroporto poderia ser usado como meio de desenvolvimento, explorando novas
		rotas e cenários de crescimento. No entanto, a existência de operações da Easyjet no Terminal 1
		complica a transferência completa de suas operações para o segundo aeroporto. Foi destacado que
		os viajantes de negócios desejam estar em Lisboa e ter acesso rápido à cidade com o mínimo de
		incerteza. Foi enfatizada a importância de não dividir as operações da companhia aérea em dois
		aeroportos, concentrando as operações de uma companhia aérea em um aeroporto. Um dos desafios
		para Portela durante a transição será o desempenho ambiental e a capacidade de ar. Para o
		segundo aeroporto, o desenvolvimento imobiliário pode ser um desafio. Os requisitos de energia e
		os aspetos ambientais, como o ruído, devem ser considerados, assim como as medidas para
		mitigá-los. Uma sugestão é usar o segundo aeroporto para impor restrições nos níveis de ruído no
		primeiro durante a noite (e possivelmente durante o dia). Para o operador, o desafio geral será
		não conseguir gerir com dois grandes aeroportos e duplicar a escala dos problemas atuais. Além
		disso, ambos os aeroportos devem ser eficientes em termos de operações e conectividade. Os
		custos devem considerar o risco de serem duplicados nesse caso (variável+funcionários+manuseio
		etc).
	</p>
	<p>
		Em ambos os casos, para facilitar o processo de transição, foi sugerido um relaxamento das taxas
		aeroportuárias, mas também a necessidade de regulamentação das taxas, embora os participantes
		não confiassem na possibilidade de uma mudança radical na regulamentação devido a aspetos
		culturais e experiências anteriores. Nenhuma companhia aérea vai querer ser a primeira a testar
		a transferência das operações para o novo terminal, as companhias aéreas devem ser encorajadas a
		mudarem-se para outro aeroporto.
	</p>
	<p>
		Deverá ser realizada uma análise de risco das operações e uma análise custo-benefício
		considerando os benefícios económicos e sociais para o país. Os riscos relacionados com questões
		legais poderão surgir devido à preservação da vida selvagem. A evolução do turismo e a exigência
		dos intervenientes do turismo poderiam também fazer surgir riscos. Finalmente, atrair
		investidores seria um desafio em ambos os casos, mas por diferentes razões.
	</p>
