	<h3>Âmbito e Objetivos</h3>
	<p>
		Um Aeroporto é uma infraestrutura com grande potencial, influencia o desenvolvimento do
		território e é por ele influenciado. São vários os tipos ou modelos de aeroportos que podem ser
		pensados para uma determinada região ou país, dependendo da visão de futuro e dos objetivos
		definidos. Importa assim alargar a discussão sobre que tipo de aeroporto melhor servirá o
		aumento de capacidade aeroportuária da região de Lisboa e que constrangimentos devem ser
		considerados. Qual o nível de integração na rede nacional de aeroportos? Qual a opção mais
		vantajosa, um único aeroporto ou um sistema de aeroportos?
	</p>
	<p>
		A localização geográfica de Portugal, a conectividade (intensidade e diversidade) das redes
		internacionais onde estamos inseridos, desde logo a ligação ferroviária à Europa, são fatores
		determinantes para a atração de investimento e, em geral, para o desenvolvimento da economia. Os
		custos de transporte são um fator determinante no mundo dos negócios, seja para transportar
		passageiros seja para mercadorias, com impacte direto no desenvolvimento regional e
		nacional.Este contexto apela à nossa reflexão, a montante de qualquer decisão: até onde queremos
		que o tráfego aéreo cresça? até onde a economia nacional depende desse crescimento? Que outras
		ações são necessárias para suportar esse crescimento ? Que tipo ou modelo de aeroporto justifica
		os investimentos a realizar? Que riscos estão associados aos investimentos necessários?
	</p>
