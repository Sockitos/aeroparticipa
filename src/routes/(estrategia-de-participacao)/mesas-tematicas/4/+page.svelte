		A dimensão ambiental também foi discutida, em particular a esperada crescente limitação de voos,
		particularmente os europeus, devido a mecanismos como o do mercado de carbono. O desenvolvimento
		tecnológico será determinante para a evolução da aviação, em particular na Europa. Assim, se
		houver o desenvolvimento de aviões sem emissões significativas espera-se um crescimento do
		transporte aéreo. Pelo contrário, se os aviões continuarem a emitir poluentes, as restrições nos
		voos intraeuropeus serão progressivamente mais restritivas.
	</p>
