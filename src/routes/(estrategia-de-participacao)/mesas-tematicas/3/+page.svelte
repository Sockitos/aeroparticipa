<PageContainer>
	<h3 class="text-2xl font-bold">Âmbito e Objetivos</h3>
	<p>
		A expansão da capacidade aeroportuária de uma região deve ser suportada por um conjunto complexo
		de condições que garantam o mínimo de risco para a atividade aeronáutica, para a segurança das
		comunidades e para as atividades económicas próximas. As condições biogeofísicas, os fenómenos
		climáticos e os riscos naturais podem condicionar a expansão aeroportuária. A biodiversidade, em
		especial por força do potencial conflito com a avifauna, pode levantar questões de segurança. As
		alterações climáticas, podem gerar aumentos de temperatura, precipitações extremas, alterações
		dos padrões de tempestade e vento, subida do nível do mar ou inundações. Simultaneamente, a
		aviação é desafiada a reduzir a sua pegada carbónica, bem como os efeitos nocivos sobre a saúde
		humana nomeadamente pelo ruído, entre outros efeitos ambientais negativos. A expansão
		aeroportuária acarreta também a impermeabilização do solo, quer por força da infraestrutura,
		quer por força dos efeitos cumulativos sobre o uso do solo nas áreas envolventes. Pelos
		compromissos assumidos pelo Estado Português no contexto nacional e comunitário, estes temas em
		torno da sustentabilidade e da resiliência revestem-se de particular relevância.
	</p>
	<p>
		Sendo necessária maior capacidade aeroportuária quais as soluções de aumento de capacidade que
		se tornam mais compatíveis com as restrições ou condicionantes ambientais?
	</p>
	<p>
		Nesta mesa temática, serão discutidos que desafios e oportunidades se colocam à expansão
		aeroportuária na região de Lisboa em matéria de ambiente e alterações climáticas.
	</p>
	<h3 class="text-2xl font-bold pt-6">
		Resultados 1ª Sessão<br />As grandes questões ambientais e perspetiva de futuro
	</h3>
	<h4 class="text-xl font-bold pt-2">
		Coordenação: Teresa Fidélis<br />Relatores: José Luís Zêzere e Jorge Cancela
	</h4>
	<h4 class="text-xl font-bold pt-2">Síntese das questões abordadas:</h4>
	<p>Primeira ronda pelos participantes: o processo e as grandes questões</p>
	<ol class="marker:text-blue-accent marker:font-bold list-disc list-outside pl-10">
		<li>
			Devem existir ou não linhas vermelhas no processo de seleção dos locais para a implantação da
			ou das novas infraestruturas aeroportuárias? As opiniões não são unanimes.
		</li>
		<li>
			Foram identificados 4 grandes questões a considerar no domínio do ambiente:
			<ol class="marker:text-blue-accent marker:font-bold list-[lower-alpha] list-outside pl-10">
				<li>Saúde humana (qualidade do ar e ruído).</li>
				<li>Ambiente, em particular a biodiversidade e as áreas protegidas.</li>
				<li>
					Risco para pessoas e bens, considerando simultaneamente os residentes e os passageiros.
				</li>
				<li>
					Ordenamento do Território – necessidade de articulação territorial da(s) nova(s)
					infraestrutura(s) aeroportuária(s) com o sistema de transportes (ferrovia e rodovia) e o
					sistema urbano.
				</li>
			</ol>
		</li>
		<li>É desejável que se encontrem várias possibilidades viáveis de solução.</li>
		<li>
			Há a consciência de que faltou a perspetiva dos operadores dos aeroportos, assim como os
			impactos territoriais das grandes opções ao nível da economia.
		</li>
		<li>
			O processo de construção e utilização da infraestrutura aeroportuária deve ser pensado para 65
			anos (15 anos de construção + 50 anos de utilização), pelo que a abordagem das questões deve
			ser prospetiva, pese embora a elevada incerteza inerente.
		</li>
	</ol>
	<p>Segunda ronda pelos participantes: perspetiva de futuro - cenário a 65 anos</p>
	<ol class="marker:text-blue-accent marker:font-bold list-disc list-outside pl-10">
		<li>
			Incerteza sobre evolução de sistemas, variáveis não possíveis de conhecer (ex. modelação de
			sistemas ecológicos).
		</li>
		<li>
			Questões de terminologia e conceitos de avaliação (ex. “climate change” são alterações ou
			mudanças climáticas).
		</li>
		<li>
			Dinâmicas territoriais, sociais, mas também legais (ex. qualidade do ar, Rede Natura 2000).
		</li>
		<li>
			Importância da valorização os sistemas que serão cada vez mais vulneráveis face à previsível
			evolução climática (ex. recursos hídricos).
		</li>
		<li>
			Estudos que já existem que consideram evolução a médio prazo (ex. riscos de tsunami para
			Lisboa, sapais e rasos-de-maré no estuário do Tejo, biodiversidade e alterações climáticas).
		</li>
		<li>
			Nesse contexto particular interesse nos “refúgios climáticos” e conectividades ecológicas
			entre eles.
		</li>
		<li>Evolução tecnológica (ex. combustíveis, ruído).</li>
		<li>
			Decisões emocionais além das racionais (ex. aumento das horas de bombas de calor dada a sua
			redução de consumo energético).
		</li>
		<li>
			Novo aeroporto como oportunidade de diversificação económica, não pensar só turismo (ex.
			cadeias logísticas de bens e serviços, “printing just in time”).
		</li>
		<li>
			Saúde pública, concentração nos fatores relevantes independentes da localização: meio
			biofísico, condições socioeconómicas, estilos de vida.
		</li>
		<li>
			Importância do reforço dos fatores de controlo de ordenamento do território e do bom exemplo
			que o Estado deve dar (ex. evolução urbana do AHD).
		</li>
		<li>
			Importância da evolução do conhecimento sobre dados geotécnicos (ex. cargas de superfície em
			terrenos instáveis).
		</li>
		<li>
			Importância da interligação entre equipamentos críticos associados ao aeroporto (ex. estação
			fluvial do Montijo inunda primeiro que aeroporto).
		</li>
		<li>
			Importância da análise de impactes irreversíveis versus mitigáveis, incorporar “custos
			escondidos” e externalidades ambientais, prever mecanismos de monitorização e adaptação.
		</li>
		<li>Não esquecer as normas internacionais de que o Estado Português é signatário.</li>
	</ol>
	<h3 class="text-2xl font-bold pt-6">
		Resultados 2ª Sessão<br />Ponderação de custos escondidos, valorização dos ecossistemas
		vulneráveis, incerteza e evolução tecnológica e dinâmicas territoriais e sociais
	</h3>
	<h4 class="text-xl font-bold pt-2">
		Coordenação: Teresa Fidélis<br />Relatores: Jorge Cancela
	</h4>
	<h4 class="text-xl font-bold pt-2">Síntese das questões abordadas:</h4>
	<p>
		Na mesa temática anterior emergiram quatro critérios relevantes para a triagem das opções
		estratégicas, designadamente a prevenção de riscos naturais, as pegadas sonora e de poluição
		atmosférica, a salvaguarda das áreas naturais e da avifauna, e importância do ordenamento do
		território e proteção do solo. Tendo presentes estes critérios, nesta mesa temática
		procuraram-se aprofundar aspetos mais complexos para a análise de opções estratégicas
		alternativas tais como os custos escondidos e valorização dos ecossistemas vulneráveis, a
		incerteza e evolução tecnológica e as dinâmicas territoriais e sociais.
	</p>
	<p class="font-bold">Sobre os custos escondidos e valorização dos sistemas vulneráveis</p>
	<p>Como fundamentar a sua importância na triagem das opções estratégicas?</p>
	<ol class="marker:text-blue-accent marker:font-bold list-disc list-outside pl-10">
		<li>
			Foi considerado fundamental estimar os custos de investimento para substituir os serviços dos
			ecossistemas afetados. São importantes ao nível dos custos ambientais e das escalas
			pertinentes (local, regional, nacional) aspetos como a afetação dos serviços dos ecossistemas,
			a degradação ambiental, a saúde humana (ruído e emissões), a perda do solo (com potencial
			económico) e qualidade de vida (mobilidade).
		</li>
		<li>
			Referiu-se também que alguns custos escondidos têm, ou podem ter, impacto decisivo no sucesso
			e adequação da infraestrutura aeroportuária, podendo ter natureza variada, incluindo custos
			não mensuráveis, custos mensuráveis (mas ainda não identificados), custos não internalizados.
		</li>
		<li>
			Foram referidos como exemplos de custos escondidos as externalidades ambientais difíceis de
			quantificar, a saúde pública devido aos fatores de ruído e poluição ambiental, oportunidades
			de melhoramento (custo do não melhoramento), congestionamento ou produtividade, custos
			ambientais decorrentes das deslocações dos trabalhadores, soluções técnicas de implementação,
			exploração e manutenção, custos de adaptação climática (migração climática, adaptação à subida
			do Nível Médio do Mar, maior escassez nas reservas de água devido ao aumento dos períodos de
			seca, ondas de calor que implicam maior esforço de climatização e adaptação dos edifícios),
			rede logística e de distribuição de energia, escassez ou esgotamento de recursos minerais
			(terras raras) que inviabilizem soluções tecnológicas adotadas a custos viáveis ou, por
			último, custos de manutenção das infraestruturas aeroportuárias e de atividades conexas, para
			adaptação às alterações climáticas e subida do nível do mar.
		</li>
	</ol>
	<p>Como analisar e com que indicadores?</p>
	<ol class="marker:text-blue-accent marker:font-bold list-disc list-outside pl-10">
		<li>
			Importa definir as condições de fronteira entre custos visíveis e escondidos. Pode haver
			variação significativa em função da informação disponível, do tempo de análise e do âmbito
			territorial.
		</li>
		<li>
			Face à evolução da exploração aeroportuária e à dinâmica dos ecossistemas, particular ênfase
			deve ser dada aos mecanismos de monitorização e avaliação, por forma a serem possíveis medidas
			compensatórias evolutivas ao longo do ciclo de vida da infraestrutura.
		</li>
		<li>
			A abordagem de análise terá de privilegiar a gestão adaptativa das respostas aos impactos em
			função dos resultados da monitorização e avaliação.
		</li>
		<li>
			Temas reconhecidamente importantes no domínio da ecologia e dos impactes sobre os ecossistemas
			incluem a poluição luminosa, o ruído, a fragmentação dos habitats, a homogeneização de usos do
			solo, a introdução de pragas, doenças e espécies invasoras.
		</li>
		<li>
			Necessidade de definir limiares que, resultando de dispositivos legais, excluem determinadas
			opções de localização.
		</li>
		<li>
			Analisando a incerteza e variabilidade e ponderando impactos, utilizando matrizes de análise
			multicritério, e recorrendo aos indicadores específicos (objetivos e subjetivos).
		</li>
	</ol>
	<p>Que cenários futuros devem ser considerados?</p>
	<ol class="marker:text-blue-accent marker:font-bold list-disc list-outside pl-10">
		<li>
			Aqueles em que os custos escondidos se revelem com grau plausível de criticidade. Em
			particular as projeções demográficas, as emissões poluentes e as alterações climáticas.
		</li>
		<li>
			A evolução tecnológica tem impacto sobre a expressão de diferentes tipologias de custos
			escondidos.
		</li>
		<li>
			Deve ser considerada a elevada incerteza dos cenários climáticos nos impactos sobre as
			populações e sobre os ecossistemas, tanto na ótica da mitigação como de adaptação.
		</li>
		<li>
			Efeito “cascata” de fatores climáticos, ambientais e riscos naturais (sismos, tsunamis,
			temporais, etc.), devido a possíveis correlações não conhecidas ou identificadas.
		</li>
		<li>
			Como resultado do cruzamento das diferentes matrizes de risco, seria muito útil a elaboração
			de uma carta de aptidão aeroportuária, multicamada, que integrasse toda a informação através
			de um “heat map”.
		</li>
	</ol>
	<p class="font-bold">Sobre a incerteza e evolução tecnológica</p>
	<p>Como fundamentar a sua importância na triagem das opções estratégicas?</p>
	<ol class="marker:text-blue-accent marker:font-bold list-disc list-outside pl-10">
		<li>
			As incertezas ambientais e climáticas, e as questões tecnológicas, principalmente devido à sua
			crescente alterabilidade e evolução acelerada, podem modificar condições mais ou menos
			estruturais e operacionais, senão mesmo paradigmas fundacionais, donde impactar diretamente a
			escolha. Entre estas incertezas encontram-se a procura do transporte aéreo, a evolução da rede
			de transportes, a evolução de fontes energéticas, o desempenho das aeronaves e demais
			tecnologia aeronáutica, a evolução do controlo do espaço aéreo, a evolução climática em termos
			globais e regionais ou o impacto da localização no ecossistema local.
		</li>
	</ol>
	<p>Como analisar e com que indicadores?</p>
	<ol class="marker:text-blue-accent marker:font-bold list-disc list-outside pl-10">
		<li>Criação de matrizes de risco (com ponderação objetiva e subjetiva).</li>
		<li>
			Incluir as incertezas na amplitude dos fatores de impacto (range das entradas nas matrizes de
			risco).
		</li>
		<li>
			Devem incluir indicadores alargados para monitorizar evolução futura: alterações nos padrões
			de comportamento de compra, semana dos 4 dias, viagens de avião mais longas e por tempos mais
			longos, AI e robótica nos sistemas.
		</li>
		<li>
			Questões que sevem ser equacionadas - que profissões existirão no futuro? que mobilidade será
			necessária? que alternativas energéticas estarão operacionais? que cenários futuros devem ser
			considerados?
		</li>
		<li>Riscos de pandemias transversais que afetem ecossistemas sensíveis.</li>
		<li>
			Tomar os cenários decorrentes da análise das matrizes citadas, mancha de maior probabilidade e
			sua correlação com outros fatores (importância estratégica, questões económicas, fatores de
			afetividade coletiva). Em complementaridade, considerar também os cenários laterais extremos,
			mais otimista e mais pessimista (com estrita relação com os “custos escondidos”).
		</li>
	</ol>
	<p class="font-bold">Sobre as dinâmicas territoriais e sociais</p>
	<p>Como fundamentar a sua importância na triagem das opções estratégicas?</p>
	<ol class="marker:text-blue-accent marker:font-bold list-disc list-outside pl-10">
		<li>
			É determinante para se perceber o resultado de benefícios e prejuízos para a população
			circunvizinha. Trata-se de uma dimensão fundamental para determinar estruturas económicas,
			eficiências do uso do solo, sistema urbano e coesão socio-territorial. É fundamental para se
			perceber e projetar pressões, fluxos, distribuição, cargas de outra natureza, que contribuem
			para fomentar (ou não) o equilíbrio territorial.
		</li>
		<li>
			Importante para se encontrar uma solução em que a oferta aeroportuária não fragmente o
			território, se articule com as redes de transporte existentes e a construir de forma a
			integrar e otimizar as redes de transporte (minimizando o impacto das deslocações) e de
			energia, preserve o território e espaços classificados (ex.: reservas naturais e agrícola
			nacional), e contribua para a geração e distribuição de riqueza.
		</li>
		<li>Relevante na prevenção de pressão sobre habitats e ecossistemas.</li>
		<li>Relevante no esforço de adaptação climática.</li>
		<li>
			Atender à capacidade indutora do processo de revisão dos IGT e correspondentes modelos de
			desenvolvimento.
		</li>
	</ol>
	<p>Como analisar e com que indicadores?</p>
	<ol class="marker:text-blue-accent marker:font-bold list-disc list-outside pl-10">
		<li>
			Necessidade de definir limiares que, resultando de dispositivos legais, excluem determinadas
			opções de localização de novas infraestruturas aeroportuárias tais como, universo de pessoas
			afetadas (positiva e negativamente), dinâmicas económicas, pressões pendulares e migratórias,
			custos e tempos de deslocações, custos relativos ao fornecimento de energia versus eficiência
			energética futura, saúde pública (decorrente do impacto ambiental), universo de habitats e
			ecossistemas afetados, impacto de adaptação climática e esforço de compensação ambiental, solo
			urbano disponível.
		</li>
	</ol>
	<p>Que cenários futuros devem ser considerados?</p>
	<ol class="marker:text-blue-accent marker:font-bold list-disc list-outside pl-10">
		<li>
			O desenho de cenários futuros deverá ter em conta diretrizes, planos, compromissos
			estabelecidos e expetativas de procura.
		</li>
		<li>
			Deverá também ter em conta avaliação dos cenários de dinâmica demográfica (sobretudo migração
			do interior para o litoral), a realidade imobiliária e a respetiva relevância para o
			desenvolvimento do território, económico (nomeadamente quanto ao turismo).
		</li>
		<li>Cenários de maior ou menor pressão dessas dinâmicas sobre habitas e ecossistemas.</li>
		<li>
			Cenários de deslocados climáticos, devido a medidas de adaptação (ex.: necessidade de
			relocalização de populações costeiras para o interior).
		</li>
	</ol>
