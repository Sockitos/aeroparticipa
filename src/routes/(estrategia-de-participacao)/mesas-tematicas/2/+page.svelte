	<h4>Sumário da discussão dos participantes</h4>
	<p>
		O aumento da capacidade aeroportuária da Região de Lisboa deverá ter em atenção a rede existente
		de aeroportos, as propostas para a LAV contidas no PFN, bem como a visão estratégica territorial
		contida no PNPOT. Fatores igualmente importantes a ter em conta são as dinâmicas de contração
		demográfica verificadas em Portugal bem como a geografia das áreas de maior valor e
		sensibilidade ambiental, em particular em toda a Região de Lisboa e Vale do Tejo.
	</p>
	<p>
		A articulação dos principais sistemas de transporte, incluindo a intermodalidade, deverá ser
		equacionada na avaliação dos níveis de acessibilidade às diversas localizações aeroportuárias em
		análise, mas também no âmbito da competição entre modos de transporte, em que cada modo se
		caracteriza por uma gama própria de distâncias mais competitivas. Em particular, e seguindo
		diretrizes europeias, espera-se que o modo ferroviário em alta velocidade venha progressivamente
		a assumir-se como solução para distâncias até 600km, deixando o modo aéreo para distâncias
		superiores.
	</p>
	<p>
		Segundo o PFN, os investimentos previstos na requalificação da Linha do Norte e na LAV, e as
		soluções técnicas adotadas para a articulação entre o modo convencional ferroviário e a alta
		velocidade, irão permitir dar resposta adequada à maioria das localizações aeroportuárias em
		discussão na região de Lisboa, quer a Norte quer a Sul do Tejo. A este respeito ficou claro que
		a chamada “Terceira Travessia do Tejo” (TTT) tem uma lógica estritamente ferroviária enquanto
		peça fundamental da rede ferroviária nacional, sendo, portanto, a sua construção independente da
		discussão da expansão da capacidade aeroportuária da região de Lisboa.
	</p>
	<p>
		Por outras palavras, temos um sistema de transportes na região que vai ser fortemente reforçado
		pelo PFN o que abre, à partida, um leque alargado de possibilidades para a localização do novo
		aeroporto, no que aos acessos e acessibilidades criadas diz respeito. Dito isto, importa ter em
		mente que falar de acessos a um aeroporto não é o mesmo que falar de acessibilidade a partir do
		aeroporto. O primeiro conceito diz respeito ao sistema de redes de infraestruturas e serviços de
		transportes, nomeadamente, os coletivos. O segundo, muitas vezes associado ao conceito de cidade
		aeroportuária, reporta-se ao conjunto de oportunidades que passariam a ser acessíveis com um
		novo aeroporto. Nesta ótica o futuro aeroporto deveria, preferencialmente, ser servido pela LAV.
	</p>
	<p>
		A questão dos acessos aos aeroportos não pode ser vista apenas do lado dos utentes e
		passageiros, mas também do lado das mercadorias e dos milhares de pessoas que se deslocam
		diariamente para o aeroporto, ou para as suas imediações, face ao enorme volume de emprego,
		direto, indireto e induzido, gerado por um aeroporto. Neste contexto, a qualidade, fiabilidade,
		rapidez e cadência dos serviços de transporte ferroviário, em particular na ligação à cidade
		principal, Lisboa, é um fator fundamental para o sucesso de um aeroporto. Em complemento, e na
		perspetiva de <i>olhar para o que temos</i>, a articulação do aeroporto com os restantes modos
		deve incluir também a rede rodoviária existente e as opções (rodoviárias) individual e coletiva,
		e a necessária provisão de estacionamentos, uma vez que o modo rodoviário continua a ser
		importante e oferece uma enorme flexibilidade aos seus utilizadores. Aliás importa garantir
		níveis elevados de redundância no funcionamento do sistema de acessos ao aeroporto. No computo
		global, dos diversos modos de transporte, o tempo de deslocação é considerado fundamental.
	</p>
	<p>
		Estudar os fluxos é tão importante quanto estudar as acessibilidades. É necessário prever
		constrangimentos futuros e prever um plano de desenvolvimento das acessibilidades face ao
		crescimento previsível de procura e aos desenvolvimentos territoriais subsequentes. Ou seja,
		deve-se salvaguardar as condicionantes territoriais para expansão de capacidade dos acessos ao
		aeroporto no futuro.
	</p>
	<p>
		Finalmente, entrando um pouco no debate sobre o modelo aeroportuário (objeto da mesa temática
		4), reconheceu-se que o país não comporta dois hubs, importando aprofundar o potencial do hub de
		Lisboa nas ligações às Américas e a África, em particular aos países de língua portuguesa, sendo
		certo que não se conhecem soluções de sucesso em que um hub se encontre baseado em dois
		aeroportos.
	</p>
