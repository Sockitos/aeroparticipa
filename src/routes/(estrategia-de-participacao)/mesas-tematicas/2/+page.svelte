		<li>
			<b>
				Em que condições se poderá justificar um transporte ferroviário em linha dedicada e
				exclusiva de serviço ao aeroporto a partir do centro de Lisboa?
			</b>
		</li>
	</ol>
	<p>
		Dependente da frequência a instalar deve garantir ligações suburbanas, regionais e de longo
		curso, sendo sempre necessário comprovar a respetiva viabilidade económica e financeira.
	</p>
	<ol type="a" start="4">
		<li>
			<b>
				Em que circunstâncias é que as ligações ferroviárias a um novo aeroporto de Lisboa
				(secundário ou principal) ficarão na dependência da realização da TTT (Terceira Travessia do
				Tejo)?
			</b>
		</li>
	</ol>
	<p>
		A lógica de integração plena ferroviária tem de incorporar uma dimensão de impacto territorial.
		A região de Lisboa apresenta níveis de desenvolvimento diferentes, nomeadamente entre a margem
		sul e a margem norte. Mas tem, como demonstram os últimos censos, focos de crescimento em torno
		de Mafra, e em torno de Palmela/Setúbal. Na lógica do desenvolvimento regional, a ferrovia deve
		assim atender à conectividade com o sistema urbano, migrar de um modelo em estrela (radial, com
		amarração em Lisboa), para um modelo circular, com distribuição sectorial. Está em causa a
		coesão territorial da AML, para a qual todos os investimentos devem contribuir. A relação entre
		o Aeroporto e a TTT é um acelerador, para ambas as infraestruturas.
	</p>
	<p>
		É nosso entendimento, sustentado no PFL, que a TTT na AML é, porventura, ”a peça mais importante
		em falta da rede ferroviária nacional, já que permite atenuar o efeito do rio Tejo como
		principal obstáculo à mobilidade entre o Sul e Lisboa, sem esquecer o Centro e Norte do País.
		Sendo um investimento localizado na capital, tem um grande efeito na coesão territorial do país
		como um todo”. Em relação à sua localização, o PFN estabelece igualmente que “já foi
		extensivamente estudada, estando consolidada a opção por um alinhamento entre Chelas, na margem
		Norte, e o Barreiro, na margem Sul”.
	</p>
