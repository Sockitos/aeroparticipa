	<h3>Âmbito e Objetivos</h3>
	<p>
		No quadro da reflexão sobre as opções alternativas para a expansão da capacidade aeroportuária
		da região de Lisboa, a articulação entre os grandes modos e sistemas de transporte pode ser
		abordada em duas vertentes.
	</p>
	<p>
		Por um lado, importa refletir, numa escala macro, sobre as dinâmicas competitivas entre modos de
		transporte, crescentemente influenciadas pelas políticas ambientais e, em particular, pelas
		políticas europeias de descarbonização dos transportes. A gama de distâncias competitivas de
		cada modo de transporte está em constante evolução, com ganhos crescentes do modo ferroviário em
		alta velocidade sobre o modo aéreo, para as chamadas curtas / médias distâncias (até 600km).
	</p>
	<p>
		A esta discussão acresce, numa perspetiva territorial, a reflexão sobre a cobertura e níveis de
		serviço da rede nacional de aeroportos, e sobre o papel que cada aeroporto deverá desempenhar
		nessa rede, designadamente o papel que deverá estar destinado à futura solução aeroportuária
		para a região de Lisboa.
	</p>
	<p>
		Por outro lado, e numa escala micro, importa igualmente refletir sobre a articulação entre modos
		de transporte e, mais especificamente, sobre as conexões das instalações aeroportuárias com as
		principais cidades e áreas urbanas que se propõem servir. Nesta perspetiva, as áreas de
		influência das instalações aeroportuárias são igualmente dinâmicas, dependendo os seus contornos
		da existência e qualidade das interfaces modais e da eficiência dos sistemas de transporte
		instalados.
	</p>
	<p>
		Esta discussão deverá ter como pano de fundo os atuais e futuros níveis de funcionamento e
		congestionamento das diversas infraestruturas e sistemas de transporte em operação na região
		metropolitana de Lisboa, bem como as propostas contidas no Plano Ferroviário Nacional em
		consulta pública, nomeadamente as opções de traçado da rede de Alta Velocidade (AV) e todo o
		debate em torno da Terceira Travessia do Tejo (TTT). A articulação de todos estes sistemas de
		transportes e a sua relevância para a decisão sobre as opções estratégicas em relação ao
		aeroporto da região de Lisboa é o objetivo desta mesa temática.
	</p>
