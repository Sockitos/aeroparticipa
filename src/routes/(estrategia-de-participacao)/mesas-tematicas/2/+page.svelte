	<p>A localização de um novo aeroporto de Lisboa</p>
	<ul>
		<li>
			deve ser dotada de ligações ferroviárias que garantam fiabilidade, conectividade de rede
			(física e funcional), frequência, e tempo de viagem competitivo com o modo rodoviário
			individual e coletivo;
		</li>
		<li>
			deve garantir ligações ferroviárias de passageiros ao nível suburbano e regional e também
			ligações ferroviárias de longo curso diretas, preferencialmente em Alta Velocidade, que
			permitam a ligação do aeroporto ao centro de Lisboa, mas também ao Norte do país, uma vez que
			a aeroporto de Lisboa serve todo o país e não apenas a região de Lisboa;
		</li>
		<li>
			deve estar habilitada para o transporte de mercadorias, sejam as de alto valor acrescentado a
			processar diretamente pelo aeroporto, sejam as que estarão associadas a um polo logístico
			criado em torno da infraestrutura aeroportuária (cidade aeroportuária), o que exige condições
			para a sua expansão.
		</li>
	</ul>
