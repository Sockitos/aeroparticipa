	<h4>Sumário da discussão dos participantes</h4>
	<p>Grupo 1 – Transporte Público</p>
	<p>
		<b>Nota prévia:</b> Os serviços de transporte público a selecionar para servir um determinado aeroporto
		estão diretamente relacionados com o modelo aeroportuário a adotar. Assim, a opção “só um aeroporto”
		e a opção “um aeroporto principal e outro secundário” terão necessariamente respostas diferentes.
		Isto porque o perfil dos passageiros é diferente: uns viajam em trabalho, sendo mais sensíveis ao
		fator tempo e ao congestionamento, menos sensíveis ao fator preço, viajando sem bagagem ou com bagagem
		reduzida, optando muitas vezes por modos de ligação em transporte público. Por sua vez, o passageiro
		que viaja em turismo é menos sensível ao fator tempo despendido nas ligações, viaja com bagagem e
		muitas vezes prefere optar pelo transporte individual. Este é um entre outros critérios que diferenciam
		as tipologias de passageiros (rendimento médio, idade, etc.) e que têm maior ou menor predominância
		num determinado modelo de aeroporto.
	</p>
	<p>
		No caso concreto português, possivelmente teremos de abordar soluções de transporte público para
		um processo que possivelmente terá diferentes fases de transição, fases essas em que podem
		coexistir com mais do que um aeroporto.
	</p>
	<ol type="a">
		<li>
			<b>
				Quais as qualidades fundamentais de um serviço público de transporte destinado a ligar um
				novo aeroporto (principal ou secundário) à cidade de Lisboa?
			</b>
		</li>
	</ol>
	<p>Serviço a um “aeroporto principal” ou “aeroporto único”:</p>
	<ul>
		<li>elevada frequência (máx. 10 min)</li>
		<li>regularidade e fiabilidade ao longo dos diferentes períodos do dia</li>
		<li>grande capacidade</li>
		<li>diversificação de modos – devido aos diferentes perfis de passageiro presentes</li>
		<li>
			tipo de serviços: TP ferroviário, TP rodoviário coletivo (serviços regulares) + serviços
			partilhados (tipo “van” com menos passageiros mas maior flexibilidade / on-demand
			porta-a-porta)
		</li>
		<li>
			serviços diretos e com poucas paragens (máx. 6 de acordo com a literatura, mas variando de
			acordo com a distância aeroporto – centro da cidade).
		</li>
	</ul>
	<p>Serviço a um “aeroporto secundário”:</p>
	<ul>
		<li>
			TP rodoviário coletivo (serviço regular) + serviços partilhados (tipo “van” com menos
			passageiros mas maior flexibilidade / on-demand porta-a-porta)
		</li>
		<li>serviços diretos e com poucas paragens.</li>
	</ul>
	<p>
		Para ambos: criação de serviços regulares para os trabalhadores do aeroporto e da cidade
		aeroportuária.
	</p>
	<ol type="a" start="2">
		<li>
			<b>
				Quais os principais fatores competitivos de um serviço público de acesso a um aeroporto,
				relativamente à concorrência do automóvel individual?
			</b>
		</li>
	</ol>
	<p>
		Frequência e tempo de viagem competitivos (definidos com base na comparação com o tempo
		despendido para realização de deslocação semelhante mas em transporte individual)
	</p>
	<p>
		Distância/tempo de deslocação curtos entre as portas de embarque e o sistema de transporte
		público (seja ferroviário ou rodoviário). Casos internacionais de aeroportos só com um terminal
		demonstram eficiências neste âmbito.
	</p>
	<p>
		Criação de áreas exclusivas no aeroporto para o acesso a TP rodoviário, à semelhança do que
		acontece com o ferroviário, evitando ineficiências associadas aos conflitos com outro tráfego
		automóvel na área do aeroporto (transporte individual, táxis/TVDE, transporte de mercadorias,
		serviços, entre outros).
	</p>
	<p>
		Competitividade do TP não depende exclusivamente de si próprio e da qualidade que lhe esteja
		associada: Restringir mais a quantidade de automóveis que pode permanecer em estacionamento no
		aeroporto (questão sensível tendo em conta a importância das receitas de estacionamento no
		modelo de gestão aeroportuária?...), criando alternativas: exemplo - park&ride em estações ao
		longo da linha ferroviária que serve diretamente o aeroporto, antecipando o estacionamento por
		parte daqueles que precisam de usar o transporte individual mas sem sobrecarregar a zona do
		aeroporto de tráfego automóvel.
	</p>
	<p>
		Equacionar: i) vias prioritárias na infraestrutura rodoviária para circulação do TP rodoviário –
		exemplo: “hov lines = high-occupancy vehicles lines”; II) bilhetes conjugados com benefícios no
		preço viagem terrestre em TP + vôo.
	</p>
	<p>
		Nota: a maior parte dos aeroportos europeus têm percentagens de repartição modal para TP
		inferiores a 50%. Seria interessante olhar para os vários casos e perceber as razões das mesmas,
		nomeadamente para o caso de Oslo em que o uso de TP atinge os 60-70%, e perceber quais os
		fatores que contribuem para esta taxa recorde.
	</p>
	<ol type="a" start="3">
		<li>
			<b> Qual o tempo máximo de serviço </b>
			<ol type="i">
				<li>
					<b> desejável, </b>
				</li>
				<li>
					<b> admissível, </b>
				</li>
			</ol>
			<b>
				(incluindo o tempo de espera e o tempo de viagem) de ligação de um novo aeroporto ao centro
				da cidade de Lisboa?
			</b>
		</li>
	</ol>
	<p>Desejável – até 30 minutos<br />Admissível – máximo 45 minutos</p>
	<p>
		Consideramos que será aconselhável coligir informação de benchmarking sobre esta matéria para
