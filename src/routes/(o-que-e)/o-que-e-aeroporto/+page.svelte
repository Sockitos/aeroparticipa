	<h3>Como é que as companhias aéreas organizam os voos?</h3>
	<p>
		O conjunto de voos e destinos regulares de uma companhia aérea constitui a sua rede aérea. Os
		voos regulares são aqueles voos que se realizam de acordo com um horário que é publicado. Por
		oposição, os voos não regulares, ou charters, não são publicados e realizam-se conforme as
		necessidades das companhias aéreas e os acordos comerciais que celebram com agentes de viagens e
		outros.
	</p>
	<p>
		Tipicamente as companhias aéreas revêm a rede aérea duas vezes por ano, época de Verão e época
		de Inverno. A seleção dos destinos depende de uma grande quantidade de fatores, incluindo: tipos
		de aviões que a companhia opera, autorizações existentes para utilizar um aeroporto (ex.: hora
		do dia, direitos comerciais de operação), expetativa de número de passageiros para os vários
		destinos, custos do combustível e outros custos, restrições ambientais, entre muitas outras.
	</p>
	<p>As redes aéreas podem ser classificadas em duas categorias principais, ver figura 1:</p>
	<ul>
		<li>
			Ponto a Ponto é uma rede aérea constituída por voos diretos, isto é, o passageiro viaja
			diretamente do aeroporto de origem para o aeroporto de destino, sem haver transbordo. Este
			tipo de redes é muito utilizada pelas companhias designadas por baixo custo (ou <i>low cost</i
			> tal como a Ryanair), embora todas as companhias pratiquem estas operações em partes das suas
			redes.
		</li>
		<li>
			<i>Hub and Spoke</i> é uma rede aérea onde todos voos estão concentrados num único aeroporto
			central, designado por aeroporto <i>hub</i>. Os outros aeroportos são designados por
			aeroportos periféricos – <i>spoke</i>. Nesta rede, os únicos voos diretos são entre o
			aeroporto <i>hub</i> e os aeroportos <i>spoke</i>. A ligação entre dois aeroportos
			<i>spoke</i> implica dois voos com transbordo no aeroporto <i>hub</i>. Este tipo de rede é
			tipicamente utilizada por companhias aéreas que não são de baixo custo. As companhias que têm
			voos intercontinentais tipicamente adotam este tipo de rede. A TAP tem implementado uma rede
			aérea desta natureza.
		</li>
	</ul>
