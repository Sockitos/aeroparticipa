	<p>
		De um ponto de técnico, um aeroporto é um aeródromo. Um aeródromo é um local que oferece as
		condições de segurança para a realização de voos. Um aeroporto é um tipo de aeródromo que está
		equipado com um conjunto de infraestruturas específicas, tais como terminal, pistas ou caminhos
		de acesso, serviços de fronteiras, etc. De forma a garantir a segurança de todos (aviões,
		pessoas, mercadorias, trabalhadores, etc.), o aeroporto está vedado e existe uma vigilância
		apertada. Assim, todos os aeroportos são aeródromos, mas nem todos aeródromos são aeroportos.
	</p>
	<p>
		Os aeroportos podem ser locais muito movimentados, aonde se realizam muitas atividades
		diferentes. Estas atividades são necessárias para garantir a segurança e comodidade das pessoas,
		e que os voos se realizam de forma segura e eficiente. Algumas destas atividades incluem:
	</p>
	<ul>
		<li>
			<span class="text-blue-accent font-bold"><i>Check-in</i> –</span> é onde os passageiros
			entregam a bagagem e obtêm os cartões de embarque antes de embarcar no avião. O
			<i>check-in</i> está tipicamente numa zona de livre acesso a todos, incluindo pessoas que não vão
			viajar, os acompanhantes dos viajantes.
		</li>
		<li>
			<span class="text-blue-accent font-bold">Segurança –</span> é o local por onde os passageiros,
			bagagem e mercadorias passam para ser garantido que nenhum objeto potencialmente perigoso segue
			a bordo. Somente os passageiros podem passar através da segurança. Assim, a segurança é o local
			que divide a zona de livre acesso da zona restrita aonde somente passageiros podem aceder.
		</li>
		<li>
			<span class="text-blue-accent font-bold">Alfândega –</span> é o local onde as pessoas passam por
			uma verificação de documentos e segurança antes de entrar ou sair do país. Não precisamos de passar
			pela alfândega quando viajamos dentro de Portugal e do Espaço Schengen, que é um conjunto de países
			da União Europeia com livre circulação.
		</li>
		<li>
			<span class="text-blue-accent font-bold">Embarque –</span> consiste na entrada dos passageiros
			no avião. Tipicamente existe uma sala de espera, na qual os passageiros aguardam enquanto são
			chamados para entrar no avião. Antes de entrar, os passageiros passam por um último momento de
			verificação, tendo de apresentar o cartão de embarque e o documento de identificação. A
			entrada no avião é muitas vezes feita através de uma estrutura metálica que liga o avião ao
			edifício (terminal). Esta estrutura chama-se ponte, ou <i>finger</i>.
		</li>
		<li>
			<span class="text-blue-accent font-bold">Desembarque –</span> consiste na saída dos passageiros
			do avião. Pontualmente poderá haver necessidade de os passageiros voltarem a apresentar o cartão
			de embarque e documento de identificação, por razões de segurança.
		</li>
	</ul>
	<p>
		Num aeroporto podemos ainda encontrar muitas outras atividades que não são necessárias para
		garantir a segurança de todos, mas que melhoram a comodidade das pessoas e podem ser importantes
		fontes de receitas. Alguns destas atividades incluem:
	</p>
	<ul>
		<li>
			<span class="text-blue-accent font-bold">Balcão de Informações e Turismo –</span> onde as pessoas
			podem perguntar sobre voos, direções, e outras informações relacionadas com o aeroporto, a cidade
			e a região.
		</li>
		<li>
			<span class="text-blue-accent font-bold">Lojas e restaurantes –</span> onde as pessoas podem fazer
			compras ou comer enquanto esperam pelos seus voos.
		</li>
		<li>
			<span class="text-blue-accent font-bold">Hotéis e centros de conferências –</span> onde as pessoas
			podem dormir ou realizar eventos e reuniões entre voos.
		</li>
		<li>
			<span class="text-blue-accent font-bold"
				>Ligação a outros modos de transporte e estacionamento –</span
			> é fundamental garantir que as pessoas conseguem chegar e sair do aeroporto rapidamente e com
			comodidade. Para isso, os aeroportos devem dispor de estacionamento e serem servidos por outros
			modos de transporte, tal como: comboio, metropolitano, autocarro, táxi ou serviços partilhados
			(ex.: Uber).
		</li>
	</ul>
	<h3>Quais são as principais zonas de um aeroporto?</h3>
	<p>Em termos gerais podemos organizar os aeroportos em</p>
	<ul>
		<li>
			<span class="text-blue-accent font-bold">Lado terra –</span> é a zona correspondente às áreas de
			livre acesso a todas as pessoas. Como exemplos podemos apontar os parques de estacionamento, as
			zonas de largada ou recolha de passageiros, ou a praça de táxis.
		</li>
		<li>
			<span class="text-blue-accent font-bold">Lado ar –</span> é a zona de acesso limitado a pessoas
			devidamente acreditadas. Como exemplo podemos identificar a placa, que é a área onde os aviões
			estão parqueados, as pistas, ou as áreas de verificação da bagagem de porão ou de mercadorias.
		</li>
	</ul>
	<p>
		Os edifícios onde as pessoas se deslocam são os Terminais. O terminal tem tipicamente um lado
		terra e um lado ar. A divisão entre os dois lado é feita pela Segurança. Assim, o lado terra do
		terminal corresponde à área entre a entrada e a segurança, enquanto o lado ar corresponde à área
		entre a segurança e as portas de embarque e desembarque. Qualquer pessoa pode aceder ao lado
		terra, mas somente passageiros podem aceder ao lado ar.
	</p>
	<p>
		Os aeroportos são facilmente reconhecíveis, ou porque já viajámos de avião, ou porque já levámos
		ou fomos buscar um amigo ou familiar, ou porque já vimos fotografias e vídeos.
	</p>
	<p>
		Todos partilham aspetos em comum, tal como as vias de acesso, o terminal, a vedação ou a pista.
		Ainda assim, são bastante diferentes uns dos outros. Uns têm mais pistas, mais terminais ou
		parques de estacionamento.
	</p>
	<p>
		Isso acontece porque os aeroportos são construídos para desempenhar diferentes funções, e de
		acordo com as necessidades dos passageiros, das companhias aéreas e das regiões.
	</p>
	<p>
		Por exemplo, um aeroporto que sirva uma região remota, na qual se espera uma quantidade reduzida
		de voos e de passageiros vai, naturalmente, precisar de menos terminais e pistas do que um
		aeroporto que sirva uma grande região metropolitana, com uma elevada quantidade de voos e
		passageiros. Isto é porque os voos que partem ou chegam ao aeroporto mais pequeno são geralmente
		em menor escala e servem uma área geográfica menor. Já um grande aeroporto tem muito mais
		terminais e pistas, pois serve uma grande quantidade de passageiros que viajam para e de vários
		destinos diferentes na Europa e no Mundo.
	</p>
	<p>
		Além disso, os aeroportos também são construídos tendo em consideração os aviões que vão ser
		utilizados pelas companhias aéreas. Os aviões mais pequenos precisam de pistas mais curtas e
		estreitas, quando comparados com grandes aviões. Portanto, aeroportos de grandes dimensões
		precisam de ter as pistas suficientemente compridas e largas para acomodar todo o tipo de
		aviões, enquanto os aeroportos mais pequenos também recebem aviões mais pequenos.
	</p>
	<p>
		Como a tecnologia evolui muito rapidamente também temos de pensar que tipo de aviões vamos ter
		no futuro, para que o aeroporto possa acompanhar essa evolução.
