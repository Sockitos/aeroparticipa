		O plano global de segurança da aviação (GASP) da ICAO apresenta a estratégia de apoio à
		priorização e melhoria contínua da segurança da aviação. O GASP define os objetivos e metas e
		descreve as principais iniciativas destinadas a melhorar a segurança aos níveis internacional,
		regional e nacional.
	</p>
