O tempo de acesso ao aeroporto influencia a procura de transporte aéreo, tanto para residentes como para visitantes, reduzindo-se a procura à medida que aumentam os tempos de acesso. Por outro lado, a deslocalização altera igualmente as condições de competição entre os vários aeroportos, como ocorre no caso presente com os aeroportos do Porto e Faro em competição com o de Lisboa, sobretudo para passageiros da região Centro, no caso do Porto, e do Alentejo, no caso de Faro.
