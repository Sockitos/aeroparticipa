	<h3>PT5 - Estudos Economico-Financeiros</h3>
	<ol class="space-y-4">
		<li>
			<p><b> <h4>O aeroporto paga-se a si próprio?</h4></b></p>
			<p>
				A avaliação financeira das opções estratégicas demonstra que o VAL (Valor Atual Líquido)  é positivo, isto é, que o investimento, financiado com dívida e capital próprio, é remunerado adequadamente, mesmo quando se consideram cenários menos favoráveis, nomeadamente de procura, de juros ou de custo de construção.
O VAL positivo corresponde ao valor que um promotor estaria disposto a pagar pela concessão com início em 2024 e conclusão em dois dos horizontes temporais estudados (2062 e 2082).
			</p>
		</li>
		<li>
			<p>
				<b>
					<h4>O VAL calculado na análise financeira corresponde ao VAL para o atual concessionário (ANA Aeroportos de Portugal)?</h4>
				</b>
			</p>
			<p>
				Não. O VAL calculado assume taxas aeroportuárias fixas a preços constantes, o que é diferente do estipulado no Contrato de Concessão (CC). Adicionalmente, não se considera o mecanismo de partilha de receitas brutas previsto no CC.
Ou seja, na avaliação financeira, o atual concessionário ponderará os benefícios resultantes da renegociação do mecanismo de regulação das taxas aeroportuárias com a alternativa de manter as atuais condições contratuais, considerando todos os efeitos da decisão, nomeadamente os que resultam do CC.

			</p>
		</li>
		<li>
			<p>
				<b>
					<h4>Quem determina as taxas aeroportuárias?</h4>
				</b>
			</p>
			<p>
				AAs taxas aeroportuárias são compostas por um conjunto variado e extenso de diferentes taxas (e.g. taxas de aterragem e descolagem, taxas de estacionamento, taxas de serviço aos passageiros, entre várias outras).
				As taxas aeroportuárias são revistas anualmente, sendo propostas pelo concessionário (ANA Aeroportos de Portugal) e sujeitas à aprovação do regulador ANAC. Se existir divergência no cálculo, as taxas a aplicar são as definidas pela ANAC. A proposta do concessionário, e a análise da ANAC, são balizadas pela aplicação de um mecanismo previsto no contrato de concessão.
				Esse mecanismo fixa uma variação média máxima para o conjunto das taxas. O concessionário tem liberdade para, dentro do limite médio máximo admissível, alterar cada uma das taxas de forma a otimizar, do ponto de vista operacional e ambiental, a utilização do aeroporto.
				
			</p>
		</li>
		<li>
			<p>
				<b>
					<h4>Um novo aeroporto significa taxas aeroportuárias mais elevadas?</h4>
				</b>
			</p>
			<p>
				As taxas aeroportuárias são um elemento relevante para o financiamento dos investimentos de aumento de capacidade. Existem receitas adicionais, como as que resultam das atividades não aeroportuárias, de que são exemplo o aluguer de espaços e as receitas do retalho ou do estacionamento automóvel.
Em tese, para garantir o mesmo retorno sobre o investimento, quanto menor o volume de investimento necessário, menor o valor das taxas. Para efeitos de comparação das opções, a análise financeira realizada considerou que as taxas se manteriam fixas a preços constantes, variando apenas de acordo com a inflação, de que resultou a conclusão que, para esse nível das taxas (constantes), todas as opções estratégicas não necessitam de subsídios públicos,
mas conduzem a níveis de retorno distintos para o concessionário.
O pressuposto de manter as taxas fixas (a preços constantes) resultou da constatação de que o mecanismo atual previsto no contrato de concessão não é adequado e, simultaneamente, garantir uma comparação neutra entre as várias opções.
Todavia, a relevância das taxas aeroportuárias não se restringe ao financiamento. São, também, um importante instrumento de competitividade para o aeroporto. Taxas mais baixas tornam o aeroporto mais atrativo e, consequentemente, promovem mais ligações, mais conectividade e mais passageiros.
			</p>
		</li>
	</ol>
	<h3>PT6 - Juridico</h3>
	<ol class="space-y-4">
		<li>
			<p><b> <h4>O contrato é uma condicionante da localização do novo aeroporto?</h4></b></p>
			<p>
				Sim. O contrato de concessão está em vigor e tem um capítulo sobre a expansão aeroportuária da
região de Lisboa, pelo que qualquer solução que a  pretenda concretizar tem de considerar as cláusulas do contrato.

			</p>
		</li>
		<li>
			<p>
				<b>
					<h4>O que é o direito de opção da Concessionária no contexto da expansão aeroportuária da Região de Lisboa? </h4>
				</b>
			</p>
			<p>
				O direito de opção significa que a Concessionária tem preferência em relação à implementação futura da solução aeroportuária para a Região de Lisboa.
			</p>
		</li>
		<li>
			<p>
				<b>
					<h4>O direito de opção é absoluto?</h4>
				</b>
			</p>
			<p>
				Não. O direito de opção não é absoluto. Existe uma limitação territorial (75km) dentro da qual funciona o direito, até ao Termo da Opção (ver FAQ PT6 nº7). A proteção territorial é uma natural característica dos contratos de concessão que se destina a proteger os direitos de exploração da Concessionária porquanto o concessionário prossegue, em vez do concedente, o interesse público regulado no contrato, juntando-se a esse interesse, o do concessionário em ter lucro na concessão. (Ver cláusula 5.ª)
			</p>
		</li>
		<li>
			<p>
				<b>
					<h4>Como pode ser exercido o direito de opção/preferência pela Concessionária? </h4>
				</b>
			</p>
			<p>
				No presente momento, o contrato admite duas formas de desenvolver novas estruturas, dentro do limite territorial (75Km):
a) como NAL; ou
b) como Alternativa da Concessionária para o NAL (ver questão sobre Alternativa da Concessionária).

			</p>
		</li>
		<li>
			<p>
				<b>
					<h4>Há direito de opção para lá dos 75km?</h4>
				</b>
			</p>
			<p>
				Não. A previsão dos 75km apenas faz sentido se referida a localização e não a área de influência. O NAL deve situar-se na área ou zona de Lisboa e não apenas servir a área ou zona de Lisboa. O âmbito territorial de proteção define-se em torno de infraestruturas existentes. O direito de opção, instrumento de defesa da exclusividade da concessão, só funciona, por conseguinte, dentro da constrição territorial.
			</p>
		</li>
		<li>
			<p>
				<b>
					<h4>Quando termina o direito de opção?</h4>
				</b>
			</p>
			<p>
				O direito de opção, de acordo com o contrato, termina quando verificada uma das seguintes hipóteses:
a) A Concessionária comunicou ao Concedente não ter capacidade para rever a sua Candidatura ao NAL nos termos por aquele solicitados; ou
b) A ausência de Aprovação Final do NAL pelo Concedente no prazo de 12 meses (ou de 18 meses, em caso de prorrogação) a contar da data da Aprovação Provisória da Candidatura ao NAL (ver questão sobre o procedimento do NAL).

			</p>
		</li>
		<li>
			<p>
				<b>
					<h4>O que significa o Termo da Opção?</h4>
				</b>
			</p>
			<p>
				O Termo da Opção liberta o concedente para avançar com a Alternativa do Concedente.
Quando ocorrer, pode haver recurso a terceiros, na área que entender mais adequada, podendo proceder à modificação do Contrato ou, na falta de acordo com a Concessionária, resolver o contrato, com compensação equivalente à do incumprimento do concedente.

			</p>
		</li>
		<li>
			<p>
				<b>
					<h4>Como funciona o procedimento do NAL?</h4>
				</b>
			</p>
			<p>
				No momento factual presente, o Concedente pode retomar o procedimento do NAL que se encontra suspenso na fase do Relatório Inicial. Tem várias etapas procedimentais, sendo um procedimento em que existem várias oportunidades de negociação. Para o detalhe procedimental, cfr. o Anexo II do Relatório Técnico do PT6.
De forma muito breve, correriam 6 meses para o Relatório Inicial, 30 dias para requerer apresentação de candidatura, 36 meses para apresentação de candidatura, 90 dias para aprovação provisória e 12 meses para aprovação final). Isto sem prorrogações nem pedidos de esclarecimentos. Decorridas estas fases, e chegado o momento da aprovação final sem que tal ocorra, verifica-se o Termo da Opção.

			</p>
		</li>
		<li>
			<p>
				<b>
					<h4>Quando pode surgir a Alternativa da Concessionária?</h4>
				</b>
			</p>
			<p>
				A Alternativa da Concessionária para o NAL pode iniciar-se a qualquer momento [até à celebração de acordos vinculativos para o desenvolvimento do NAL enquanto envida esforços para apurar se existem locais alternativos mais eficientes e menos dispendiosas do que o desenvolvimento do NAL – “proposta de modificação da Concessionária”] ou, no procedimento do NAL, quando este for retomado.
			</p>
		</li>
		<li>
			<p>
				<b>
					<h4>O que implica a Alternativa da Concessionária se for aceite?</h4>
				</b>
			</p>
			<p>
				Se for aceite pelo Concedente, implica a Modificação da Concessionária ao contrato e confere-lhe o direito ao reequilíbrio financeiro de acordo com a previsão da cláusula 25.ª.
			</p>
		</li>
		<li>
			<p>
				<b>
					<h4>Quais são as formas de reequilíbrio financeiro?</h4>
				</b>
			</p>
			<p>
				Existem vários instrumentos para operar o reequilíbrio financeiro: alteração de taxas sujeitas a regulação económica, nos termos do anexo 12 e com consulta à reguladora; comparticipação ou compensação pelo Concedente; prorrogação de prazo da concessão ou qualquer outra forma acordada entre as partes.
Importa sublinhar que o contrato impõe que a Concessionária demonstre que se está a esforçar para mitigar os efeitos da modificação.

			</p>
		</li>
		<li>
			<p>
				<b>
					<h4>Neste momento, o Concedente está comprometido com a solução Montijo?</h4>
				</b>
			</p>
			<p>
				Não. Em 2017, iniciou-se um processo negocial, pela Concessionária, que comunicou ao Concedente a sua intenção de apresentar uma proposta de expansão aeroportuária, antecipando a verificação de um Fator de Desencadeamento. Propunha a construção de um aeroporto complementar em Montijo. Configurava uma Alternativa da Concessionária ao NAL, ao abrigo do disposto na cláusula 42.3. do Contrato de Concessão. Foi celebrado, em consequência, um Memorando onde foi regulado o procedimento. Depois da apresentação, pela Concessionária, uma proposta de desenvolvimento, conceção, financiamento, construção e operação” do AHD+Montijo, foi constituída uma comissão de negociação (eventual modificação do contrato para acomodar a implementação dessa Alternativa). Em 2018, verificou-se o Fator de Desencadeamento. As Partes celebraram o Acordo Complementar n.º 1 e com os avanços negociais, em 2019, o Acordo Complementar n.º 2. A Agência Portuguesa do Ambiente emitiu em 2020, uma Declaração de Impacte Ambiental (DIA) condicionada para a opção em negociação. O processo negocial não avançou.
A ANA comunicou ao Estado, por carta de 12.04.2021, entender que “o Processo Negocial, enquadrado e regulado pelo Memorando de Entendimento, pelo Acordo Complementar n.º 1 e pelo Acordo Complementar n.º 2, se encontra extinto, por caducidade”.
A caducidade de atos jurídicos significa que eles deixam de produzir efeitos. Portanto o Memorando e os Acordos Complementares não estão em vigor desde 12.04.2021. [de acordo com a documentação que a CTI e seus colaboradores tiveram acesso]. Paralelamente ao processo negocial descrito, foi iniciado o procedimento do NAL: 2018, notificação de Fator de Desencadeamento (cláusula 44.2 do contrato); a Concessionária foi notificada para elaborar o Relatório Inicial (no mês subsequente); em maio, a título excecional, porque a Alternativa da Concessionária estava a correr, o Concedente suspendeu o prazo de apresentação do referido Relatório. Esta suspensão chegou a ser prorrogada várias vezes. Apesar da complexidade jurídica de interpretação de cláusulas sobre a suspensão do prazo, a solução que melhor acomoda o princípio da boa fé é a que considera que retoma do prazo para apresentação do Relatório Inicial apenas ocorrerá se e quando o Concedente notificar a Concessionária para o efeito. Portanto, o procedimento do NAL está na disponibilidade do Concedente, não se tendo verificado ainda o Termo da Opção.

			</p>
		</li>
		<li>
			<p>
				<b>
					<h4>O que acontece se o Concedente não respeitar o Direito de Opção da Concessionária?</h4>
				</b>
			</p>
			<p>
				Se o Concedente não respeitar o direito de opção da Concessionária porque, por exemplo, desenvolve ou autoriza que terceiros desenvolvam um aeroporto dentro dos 75Km e antes do Termo da Opção, incumpre o contrato.
Nessa hipótese:
a) Concessionária teria direito à reposição do equilíbrio financeiro, desde que verificadas todas as condições contratuais aplicáveis, incluindo o Limite Mínimo para Acionar o Reequilíbrio;
b) se o reequilíbrio fosse muito oneroso para o Concedente, este poderia resolver o contrato, isto é, pôr fim ao contrato, cumprindo formalidades de notificação e pagando uma indemnização à Concessionária que seria calculada como se a resolução fosse por incumprimento do Concedente.

			</p>
		</li>
		<li>
			<p>
				<b>
					<h4>Em que circunstâncias pode haver resolução do contrato de concessão?</h4>
				</b>
			</p>
			<p>
				O contrato pode ter fim, através de resolução, nas seguintes situações com relevo para este momento:
a) por razões de interesse público, caso em que a resolução toma o nome de resgate – ver PT6 FAQ nº16.
b) por incumprimento da Concessionária (resolução sancionatória) de determinadas obrigações identificadas no contrato;
c) no âmbito do procedimento do NAL, verificado o Termo da Opção;
d) incumprimento do Concedente no contexto de violação do Termo da Opção, caso a indemnização devida se mostrasse excessivamente onerosa para o Concedente.
Importa sinalizar que a resolução do contrato é um cenário disruptivo grave porque afetaria todos os aeroportos geridos pela Concessionária.

			</p>
		</li>
		<li>
			<p>
				<b>
					<h4>Quais são as obrigações da Concessionária que, uma vez não cumpridas, conferem o
						direito à resolução do contrato?
						</h4>
				</b>
			</p>
			<p>
				Estão previstas no contrato e são as seguintes (previstas na cláusula 62.4 aí descritas com mais detalhes):
- qualquer facto que confira, nos termos do CCP, o direito do Concedente a resolver o contrato como sanção por incumprimento;
-incumprimento grave das obrigações contratuais, incluindo qualquer incumprimento que (não sendo sujeito ao regime das multas contratuais previsto no anexo 7) não acabe no prazo de 6 meses a contar da notificação do Concedente ou se verifique em 5 situações consecutivas da mesma natureza;
-incumprimento integral ou parcial do pagamento inicial;
-incumprimento dos contratos de financiamento;
-dissolução ou pedido de declaração de insolvência da Concessionária;
-cessação da prestação de atividades e serviços aeroportuários ou o abandono, total ou parcial, da exploração de qualquer aeroporto ou das obrigações de desenvolvimento (após o seu início);
-incumprimento grave de uma declaração ou garantia prevista na cláusula 4.ª;
-incumprimento das cláusulas 10.ª (forma e regime jurídico da concessionária) e 54.ª (direitos de prestar atividades e serviços aeroportuários);
-incumprimento de obrigações relacionadas com rácios de dívida;
-cobrança de taxas aeroportuárias ou outras contrapartidas em montantes superiores aos permitidos pelo anexo 12;
- falta de seguros; e aplicação de multas contratuais, durante 2 ou mais anos consecutivos (cláusula 59.3);
Contudo, o contrato prevê que, antes da resolução,
a) notificar o concedente;
b) sanar o incumprimento, sendo possível, dentro de determinados prazos;
c) pode haver acordo entre concessionária e concedente para a sanação;
d) se não houver sanção nos prazos ou dentro do acordo da anterior alínea, o concedente pode então resolver o contrato e/ou intervir na concessão (cláusula 62.5).


		</li>
		<li>
			<p>
				<b>
					<h4>Se a Concessionária não concordar com a opção de localização do Concedente, pode acionar-se o mecanismo do resgate?</h4>
				</b>
			</p>
			<p>
				Neste momento, não.
O resgate é o nome que toma o instrumento jurídico que põe fim ao contrato por razões de interesse público. Assim, nos termos do contrato (cláusula 64.ª) para operar o resgate é preciso:
a) que se verifique, de forma fundamentada, isto é, explicada com factos e argumentos, o interesse público (de que serve melhor o interesse público a exploração aeroportuária passar para o Estado); 
b) ter decorrido o prazo de 25 anos sobre a data da assinatura, ou seja, tendo sido o contrato assinado em 2012, só pode ocorrer em 2037;
c) comunicar, por escrito, à Concessionária com 6 meses de antecedência.
Não se verificam, portanto, todos os requisitos para um resgate, desde logo o prazo. A eventual não concordância da Concessionária, se for caso de exercício do Direito de Opção, com a localização escolhida pelo Concedente, deve ser tratada à luz dos mecanismos contratuais previstos para a expansão aeroportuária da região de Lisboa.
