Por fim, a operação em regime dual condiciona a procura dos passageiros em trânsito, nomeadamente para aqueles em que o voo anterior chega a um dos aeroportos e o voo posterior parta do outro aeroporto, mas também para todos os que optam por realizar a sua transferência num aeroporto menos congestionado.

