O volume de passageiros na última década no AHD duplicou entre 2012 e 2019, a uma taxa de crescimento anual superior a 10% ao ano. Os valores de crescimento da procura aeroportuária ao longo do período entre 2013 e 2019 foram sempre superiores a 2 milhões de passageiros ao ano, atingindo-se o valor acrescido de 4 milhões de passageiros no ano de 2017. A análise histórica da evolução das taxas de crescimento registadas no Aeroporto Humberto Delgado face às taxas de crescimento do volume de passageiros na região europeia mostra que as taxas de crescimento em Lisboa foram sistematicamente o dobro das observadas naquela região ao longo da última década.
As projeções desenvolvidas assumem que essa diferença diminuirá até 2050, passando do dobro para zero. Considerando esta convergência ajustaram-se as taxas de crescimento projetadas pelas diferentes entidades internacionais até 2050 para o crescimento de passageiros na região europeia. O crescimento anual projetado para Lisboa no cenário baixo até 2050 é de 2,81%, no cenário central de 3,77% e no cenário alto de 4,71%, todas elas muito mais reduzidas do que as verificadas na última década no AHD.
