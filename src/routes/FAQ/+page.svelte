	<h3>PT5 - Estudos Economico-Financeiros</h3>
	<ol class="space-y-4">
		<li>
			<p><b> <h4>O aeroporto paga-se a si próprio?</h4></b></p>
			<p>
				A avaliação financeira das opções estratégicas demostra que o VAL é positivo, isto é, que o
				investimento, financiado com dívida e capital próprio, é remunerado adequadamente,
				mesmo quando se consideram cenários menos favoráveis, nomeadamente de procura, de
				juros ou de custo de construção.
				O VAL positivo corresponde ao valor que um promotor estaria disposto a pagar pela
				concessão com início em 2024 e conclusão em dois dos horizontes temporais estudados
				(2062 e 2082), com uma estrutura de capitais com um rácio dívida/capital próprio de 34%,
				ao longo de todo o horizonte temporal.
			</p>
		</li>
		<li>
			<p>
				<b>
					<h4>O VAL calculado na análise financeira corresponde ao VAL para o atual concessionário (ANA/VINCI)?</h4>
				</b>
			</p>
			<p>
				Não. O VAL calculado assume taxas aeroportuárias fixas a preços constantes, o que é
				diferente do estipulado no Contrato de Concessão (CC). Adicionalmente não se considera o
				mecanismo de partilha de receitas brutas previsto no CC.
				Ou seja, na avaliação financeira, o atual concessionário ponderará os benefícios resultantes
				da renegociação do mecanismo de regulação das taxas aeroportuárias com a alternativa de
				manter as atuais condições contratuais, considerando todos os efeitos da decisão,
				nomeadamente os que resultam do CC. Estes benefícios e custos não foram estimados,
				podendo resultar quer num valor superior quer num valor inferior ao VAL estimado.
			</p>
		</li>
		<li>
			<p>
				<b>
					<h4>Quem determina as taxas aeroportuárias?</h4>
				</b>
			</p>
			<p>
				As taxas aeroportuárias são compostas por um conjunto variado e extenso de diferentes
				taxas (e.g. taxas de aterragem e descolagem, taxas de estacionamento, taxas de serviço aos
				passageiros, entre várias outras).
				As taxas aeroportuárias são revistas anualmente, sendo propostas pelo concessionário
				(ANA/VINCI) e sujeitas à aprovação do regulador ANAC. Se existir divergência no cálculo, as
				taxas a aplicar são as definidas pela ANAC. A proposta do concessionário, e a análise da
				ANAC, são balizadas pela aplicação de um mecanismo previsto no contrato de concessão.
				Esse mecanismo fixa uma variação média máxima para o conjunto das taxas. O
				concessionário tem liberdade para, dentro do limite médio máximo admissível, alterar cada
				uma das taxas de forma a otimizar, do ponto de vista operacional e ambiental, a utilização do aeroporto.
			</p>
		</li>
		<li>
			<p>
				<b>
					<h4>Um novo aeroporto significa taxas aeroportuárias mais elevadas?</h4>
				</b>
			</p>
			<p>
				As taxas aeroportuárias são um elemento relevante para o financiamento dos investimentos
				de aumento de capacidade. Existem receitas adicionais, como as que resultam das
				atividades não aeroportuárias, de que são exemplo as receitas do retalho ou do
				estacionamento.
				Em tese, para garantir o mesmo retorno sobre o investimento, quanto menor o volume de
				investimento necessário, menor o valor das taxas. Para efeitos de comparação das opções, a
				análise financeira realizada considerou que as taxas se manteriam fixas a preços constantes,
				variando apenas de acordo com a inflação, de que resultou a conclusão que, para esse nível
				das taxas (constantes), todas as opções estratégicas não necessitam de subsídios públicos,
				mas conduzem a níveis de retorno distintos para o concessionário.
				O pressuposto de manter as taxas fixas (a preços constantes) resultou da constatação que o
				mecanismo atual previsto no contrato de concessão não é adequado e, simultaneamente,
				garantir uma comparação neutra entre as várias opções.
				Todavia, a relevância das taxas aeroportuárias não se restringe ao financiamento. São,
				também, um importante instrumento de competitividade para o aeroporto. Taxas mais
				baixas tornam o aeroporto mais atrativo e, consequentemente, promovem mais ligações,
				mais conectividade e mais passageiros.
			</p>
		</li>
