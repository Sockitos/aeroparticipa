	<h3>PT4 - Ambiente</h3>
	<ol class="space-y-4">
		<li>
			<p><b> <h4>Existe alguma boa localização para um novo aeroporto de Lisboa, do ponto de vista ambiental?</h4></b></p>
			<p>
				A localização de um aeroporto gera sempre elevados problemas ambientais, seja sobre a saúde humana seja sobre a preservação dos valores naturais. Estes problemas resultam da implantação no terreno, dos efeitos da movimentação das aeronaves, do tráfego gerado pelos passageiros e mercadorias, entre outros. As localizações podem também ser vulneráveis aos efeitos de riscos naturais e tecnológicos. Neste contexto, e atendendo às caraterísticas territoriais e ambientais em torno da cidade de Lisboa, não há localizações ideais nem perfeitas. Ainda assim, há localizações mais favoráveis em relação a cada critério ambiental estudado. Os critérios ambientais usados na avaliação estratégica, discutidos nas mesas temáticas e objeto de participação pública, focaram-se em informação ambiental fundamental, capaz de caracterizar e, sobretudo, diferenciar as vulnerabilidades ambientais de cada opção estratégica face aos potenciais efeitos gerados pela expansão da capacidade aeroportuária. Quando se encontram afastadas da população afetam mais valores naturais importantes, quando estão mais junto de áreas urbanas afetam mais população. Por estes motivos, a avaliação desenvolvida pela CTI resulta de uma análise integrada e de exercício comparativo. Procurou-se destacar as soluções que sendo menos gravosas para os valores ambientais permitem valorizar outros aspetos como a segurança aeronáutica, a capacidade de resposta à procura, a acessibilidade e a valorização do potencial de desenvolvimento económico.
			</p>
		</li>
		<li>
			<p>
				<b>
					<h4>A intenção de construir um novo aeroporto contraria as políticas públicas em matéria de alterações climáticas?</h4>
				</b>
			</p>
			<p>
				Esta questão deve ter em atenção duas perspectivas, a da mitigação e a da adaptação. Na perspectiva da mitigação, colocam-se prioridades sobre a redução de emissões de gases com efeito de estufa associados ao transporte aéreo, que representa cerca de 13,9% das emissões, imediatamente a seguir ao transporte rodoviário. Está a observar-se uma evolução tecnológica no domínio dos combustíveis SAF (Sustainable Aviation Fuel), já em utilização, na inovação dos motores de propulsão para melhorar o seu desempenho e reduzir externalidades, entre outros. O CORSIA (Carbon offsetting and Reduction Scheme for International Aviation) um mecanismo de mercado global para a redução das emissões vai continuar a contribuir para este efeito. Outras ações estão em curso no domínio da utilização da eletricidade e do hidrogénio, ambas ainda em fases de teste. A Comissão Europeia, foi clara na pretensão de reduzir a utilização do transporte aéreo, favorecendo o transporte ferroviário como alternativa. No centro da Europa isso será concretizado, mas em localizações periféricas, como é o caso de Portugal, esta transição modal terá menor impacto, absorvendo apenas as viagens internas no Continente e porventura até Madrid, que representam uma pequena percentagem do total de viagens aéreas, cerca de 5% do total de voos (125 voos / semana / sentido, de acordo com o relatório da procura PT1 relatório 1, Anexo 2, pag 15). 
Na perspetiva da adaptação colocam-se prioridades na resiliência das infraestruturas, nomeadamente através do reforço de segurança aos efeitos negativos dos fenómenos extremos. Face aos efeitos gerados pelas alterações climáticas (por exemplo a previsível subida do nível médio da água do mar, ocorrência de fenómenos extremos como as inundações, entre outros) a localização das infraestruturas de transporte aéreo, como os aeroportos devem observar-se em zonas menos vulneráveis, sob pena de ameaçarem a segurança da atividade aeronáutica, e dos seus utilizadores.

			</p>
		</li>
		<li>
			<p><b> <h4>Porque não se avaliou a OTA?</h4></b></p>
			<p>
				Entre as razões ambientais que levaram à não avaliação da OTA, encontram-se a referida necessidade de remover o monte fronteiro a Norte, estimado em 47 milhões de m3 de terra, a destruição do Paul da Ota, e a impermeabilização de uma enorme zona húmida que alaga regularmente no inverno; ventos laterais; coincidência do enfiamento de uma das pistas com o parque de combustível de Aveiras (Companhia Logística de Aveiras), constituindo um risco para acidentes graves.
			</p>
		</li>
		<li>
			<p>
				<b>
					<h4>Quais os tipos de custos ambientais associados à manutenção do AHD? </h4>
				</b>
			</p>
			<p>
				Manter o aeroporto Humberto Delgado (AHD) significa manter os custos gerados pelo ruído e poluição do ar sobre a saúde humana numa das zonas de maior densidade populacional do País. Acresce a estes os efeitos gerados pelo tráfego rodoviário associado ao aeroporto.
			</p>
		</li>
		<li>
			<p><b> <h4>O que é preciso fazer urgentemente no AHD?</h4></b></p>
			<p>
				Do ponto de vista ambiental, o AHD deve ser fechado assim que possível, ou pelo menos reduzir significativamente o número de voos, em especial os noturnos. Deve também ser garantida a plena execução das medidas previstas no Plano de Ações de Gestão e Redução de Ruído adotado pela ANA, e assegurada a expansão da monitorização ambiental atual a outros poluentes que, devido às suas características, podem ter efeitos adversos na saúde humana e no ambiente, nomeadamente, as partículas ultrafinas, o carbono negro, os metais, e outros Hidrocarbonetos Aromáticos Policíclicos (HAPs) e a sua comunicação à sociedade. Do ponto de vista operacional foram propostas um conjunto de ações de melhoria para o AHD, que podem ser lidas no relatório de curto-prazo (Anexo 10 do relatório PACARL)
			</p>
		</li>
		<li>
			<p>
				<b>
					<h4>Quais os fundamentos ambientais que explicam porque o Montijo não pode ser solução provisória até o campo de tiro de Alcochete (CTA) avançar, como chegou a ser pensado?</h4>
				</b>
			</p>
			<p>
				Embora a localização do Montijo se apresente numa zona já artificializada e com atividade aeronáutica da Base Aérea, o que à partida poderia constituir uma mais-valia do ponto de vista ambiental, a verdade é que esta localização enferma de um conjunto de vulnerabilidades assinaláveis comparativamente com as outras analisadas pela CTI. Entre estas encontra-se a vulnerabilidade a inundações e subida do nível médio do mar, a afetação de um número significativo de população pelo ruído e poluição do ar, bem como a afetação da avifauna pelos cones de aproximação e descolagem, abundante nesta zona de estuário, incluindo a 1000 pés,
o que contraria a legislação ambiental nacional e europeia de proteção das aves. A perturbação das aves é ela própria potencial de risco para a segurança aeronáutica. 
Já no EIA do MTJ se tinha identificado a forte perturbação da avifauna. Por este motivo o EIA considera um conjunto de medidas de mitigação e de compensação inéditas no país. Embora possam ter passado despercebidas, algumas das medidas de mitigação requeridas pelo ICNF podem dificultar o regular funcionamento da atividade aeronáutica dadas as exigências de proteção dos movimentos naturais das aves. As alegações que já lá está uma infraestrutura aeronáutica são compreensíveis, mas é importante salientar que a dimensão e o número de movimentos de aeronaves é atualmente manifestamente inferior ao que será com a instalação de um aeroporto no MTJ (mesmo que apenas com uma única pista).

			</p>
		</li>
		<li>
			<p><b> <h4>Quais as principais fragilidades ambientais de Santarém e porque é pior que a localização CTA?</h4></b></p>
			<p>
				Do ponto de vista ambiental, atendendo aos critérios de avaliação usados, Santarém surge em terceiro lugar entre as soluções únicas, sempre mais vantajosas que as duais, as quais tendem a duplicar os problemas de ambiente. As desvantagens comparativas da localização Santarém com a CTA e Vendas Novas prendem-se com a maior afetação de população (comparativamente com estas localizações), com os perímetros de proteção de águas subterrâneas destinadas ao abastecimento público, com o solo agrícola e a reserva agrícola (considerada crítica numa lógica de maior autonomia nacional para a produção alimentar) e com corredores migratórios de avifauna. Do ponto de vista ambiental Santarém tem vantagens sobretudo no domínio dos riscos sísmicos em comparação com outras localizações.
			</p>
		</li>
		<li>
			<p>
				<b>
					<h4>Quais as grandes diferenças do ponto de vista ambiental entre CTA e Vendas Novas? </h4>
				</b>
			</p>
			<p>
				Embora sejam bastante semelhantes, e ambas afetem cerca de 42 000 sobreiros, as maiores diferenças residem na menor afetação em Vendas Novas de áreas de proteção e recarga de aquíferos subterrâneos e de corredores migratórios de avifauna. Observa-se também menor exposição a riscos naturais em Vendas Novas.
É verdade que a afetação de sobreiros é elevada em CTA (cerca de 42 000). Ainda assim a CTI, através do trabalho do PT2 identificou a possibilidade de deslocar para sul as primeiras pistas no CTA, permitindo com isso a redução significativa da afetação do número de sobreiros.

			</p>
		</li>
		<li>
			<p><b> <h4>O Campo de Tiro de Alcochete tem mais riscos ambientais do que Santarém? </h4></b></p>
			<p>
				Globalmente Santarém apresenta mais riscos ambientais do que o CTA. Santarém apresenta maior risco de inundação em face das características geomorfológicas, bem como de incêndio atendendo às características do uso do solo na envolvente, constantes na mais recente carta de perigosidade de incêndio do ICNF. Campo de Tiro de Alcochete tem mais riscos sísmicos.
			</p>
		</li>
		<li>
			<p>
				<b>
					<h4>O novo aeroporto afeta 250.000 sobreiros em Alcochete?</h4>
				</b>
			</p>
			<p>
				Não. A área de implantação prevista para o aeroporto no CTA afeta cerca de 42 000 sobreiros.
Ainda assim a CTI, através do trabalho do PT2 identificou a possibilidade de deslocar para sul as primeiras pistas em Alcochete, permitindo com isso a redução significativa da afetação do número de sobreiros.

			</p>
